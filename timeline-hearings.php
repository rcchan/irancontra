      <h3>August 4-6: Clarridge, George</h3>
      <ul>
       <li>The televised hearings ended on August 3. Several additional witnesses were heard - and several others recalled - in the coming weeks, including Rodney McDaniel (NSC staff member who took notes at the president’s daily intelligence briefings) and Michael Ledeen (former consultant to the NSC). Most notably, testimony was heard from three additional witnesses in closed session during August 4-6: Duane (Dewey) Clarridge (CIA count-terrorism chief), Alan Fiers, Jr. (CIA Central American Task Force chief), and Clair George (CIA director of covert operations). <a href="http://www.nytimes.com/1987/08/20/world/us-planned-in-84-for-south-africa-to-help-contras.html">Portions of Clarridge's testimony</a> were declassified and released a few weeks later. <a href="https://www.nytimes.com/1987/08/26/world/casey-didn-t-heed-warning-on-deals-iran-panel-hears.html">Portions of Fiers' and George's testimony</a> were released shortly thereafter.</li>
