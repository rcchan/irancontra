 <td><ul type="disc">
    <li>
        <b>1926</b>
        : Augusto Sandino was a member of a revolution fighting against the Conservative ruler of Nicaragua, Adolfo D&#237;az (who was backed by the U.S.) and
        continued fighting after a cease-fire. (Nicaragua)
    </li>
    <li>
        <b>1932</b>
        : The outgoing Liberal president appointed Anastasio Samoza Garc&#237;a to head the guard the National Guard; Sandino maintained some land and a small
        army that the new President gave him upon the departure of U.S. Marines. (Nicaragua)
    </li>
    <li>
        <b>1936</b>
        : Garc&#237;a takes power, and his dynasty rules for the next 43 years. (Nicaragua)
    </li>
    <li>
        <b>1953-1979</b>
        : Mohammad Reza Shah Pahlavi (known as &#8220;the Shah) is secular and authoritarian ruler of Iran. Iran is one of the United States&#8217; strongest
        allies in the Middle East. (Iran)
    </li>
    <li>
        <b>1961</b>
        : The Sandinistan National Liberation Front (FSLN; named after Sandino) is founded in opposition to the regime. Ideologically, it sees itself as a
        Marxist-Leninist vanguard organization whose goal is to create a socialist state. They were inspired by the Cuban revolution and were anti-U.S.
        intervention. (Nicaragua)
    </li>
    <li>
        <b>1967</b>
        : Anastasio Somoza Debayle comes to power. (Nicaragua)
    </li>
    <li>
        <b>1972</b>
        : An earthquake rocks Nicaragua, and President Somoza Debayle takes some relief aid for himself. (Nicaragua)
    </li>
    <li>
        <b>1974</b>
        : The FSLN launches an attack and a kidnapping, which resuls in Somoza declaring a state of siege and a brutal counterinsurgency campaign. Pedro
        Joaqu&#237;n Chamorro also founds the UDEL (Union for Democratic Liberation). (Nicaragua)
    </li>
    <li>
        <b>1977</b>
        : Jimmy Carter takes office at a time when Americans were wary of foreign intervention due to the Vietnam War. He subsequently cuts off all aid to the
        Nicaraguan government until it improves its human rights status; Somoza thus lifts the state of siege in order to receive aid. The Sandinistas renewe
        and expands their attacks against the regime with the support of some academics, businessmen, and priests, including Adolfo Calero (a businessman).
        (Nicaragua)
    </li>
    <li>
        Business elites, including Alfonso Robelo, are angry at Somoza&#8217;s failure to promote business interests and the fact that his dynasty prevents
        others from participating in politics. Major political parties are not powerful and, therefore, these people had nobody to represent their interests.
        However, the UDEL grows, attracting Robelo and Calero. (Nicaragua)
    </li>
    <li>
        <b>1978</b>
        : Riots and demonstrations break out across Iran, largely in response to the Shah&#8217;s secularism and close relationship with the U.S. (Iran)
    </li>
    <li>
        <b>January 10, 1978</b>
        : Chamorro is assassinated, setting off protests throughout the country. (Nicaragua)
    </li>
    <li>
        <b>Summer 1978</b>
        : The FAO, an umbrella group of opposition movements, is formed. It includes moderates and the more radical FSLN. (Nicaragua)
    </li>
    <li>
        <b>August 22, 1978</b>
        : Sandinistan forces, led by &#201;den Pastora, captured the National Palace where the Nicaraguan legislature is in session. Pastora gets the regime to
        give money, release prisoners, and provide means of publicizing the Sandinista cause. However, the FSLN reads a communiqu&#233; by brothers Humberto
        and Daniel Ortega, two powerful Sandinistan leaders, which suggests their Socialist aims. This frightens some moderates in the FAO. (Nicaragua)
    </li>
    <li>
        Uprisings against the state continue. The Sandinistas, with materiel support from Venezuela and Panama, continue fighting. Carter tried to get all
        sides together to find a solution to the conflict to no avail. However, the U.S. forces the FAO to moderate its positions, leading to the departure of
        the FSLN from the group. (Nicaragua)
    </li>
    <li>
        As the fighting continued, FSLN is bolstered by international opposition to the regime, as well as by Cuban support in the form of arms and military
        advising. (Nicaragua)
    </li>
    <li>
        <b>1979</b>
        : Riots and demonstrations grow increasingly numerous, frequent, and violent, ultimately culminating in the Iranian Revolution. (Iran)
    </li>
    <li>
        <b>January 1979</b>
        : The Shah leaves Iran, and the country is declared an Islamic Republic by Ayatollah Ruhollah Khomeini. The Ayatollah severs all ties with the U.S. and
        declares Israeli illegitimate. (Iran)
    </li>
    <li>
        <b>July 19, 1979</b>
        : The Sandinistas take power. They soon declare a state of emergency and expropriate land and businesses owned by the old dynastic family and their
        associates; nationalize banks, mines, transit systems; abolish the old courts, the constitution, and the legislature; organize peasants and workers
        into Civil Defense Committees; and declare that elections are unnecessary because the FSLN made the decisions. This prompted criticism from the
        Catholic Church and business interests, and the Sandinistas arrested dissidents. (Nicaragua)
    </li>
    <li>
        Carter sends $99 million in aid to the FSLN so that it would become pro-U.S. Meanwhile, Cuban officials fly to Nicaragua to advise the FSLN on foreign
        and domestic policy and the FSLN sought an alliance with the Soviet Bloc. (Nicaragua)
    </li>
    <li>
        <b>August 28, 1979</b>
        : The FSLN passes three decrees limiting the freedom of the press and political organizing. (Nicaragua)
    </li>
    <li>
        <b>September 12, 1979</b>
        : Carter releases remaining aid that is due to Nicaragua. (Nicaragua)
    </li>
    <li>
        <b>1980-90</b>
        : Iran-Iraq War. (Iran)
    </li>
    <li>
        <b>February 1980</b>
        : Alfonso Robelo and his MDN party begin vocal opposition to the regime and call for democracy, elections, and political pluralism. (Nicaragua)
    </li>
    <li>
        <b>March 1980</b>
        : The FSLN sign economic, cultural, technological, and scientific agreements with the USSR.
    </li>
    <li>
        Violent opposition to the new regime begins, and attacks are carried out by peasants and farmers wary of the new regime&#8217;s agrarian policies.
        (Nicaragua)
    </li>
    <li>
        <b>Early 1980</b>
        : Once it becomes clear to policymakers in Washington that the FSLN would not moderate its position, Carter authorizing a finding in allowing the CIA
        to support resistance forces in Nicaragua with assistance for organizing and propaganda purposes but not for armed action. (Nicaragua)
    </li>
    <li>
        <b>Mid-1980</b>
        : Jos&#233; Cardenal and Enrique Berm&#250;dez form what would become the Nicaraguan Democratic Force, or FDN (the main contra group). (Nicaragua)
    </li>
    <li>
        <b>1980</b>
        : It becomes clear that the FSLN is supporting revolutionaries in neighboring El Salvador. Carter initially ignores this and continues to give the
        Sandinistas aid in order to curry favor. (Nicaragua)
    </li>
    <li>
        <b>January 20, 1981</b>
        : Reagan is inaugurated in the context of a rightward shift in U.S. politics and concern about the Soviet Union. Due to the continued unwillingness of
        the Sandinistas to stop their support of Salvadoran rebels, Reagan soon cuts off all aid to the FSLN indefinitely. This prompts the FSLN to suggest
        that the U.S. would invade, thereby justifying further consolidation of power and crackdowns on perceived enemies. (Nicaragua)
    </li>
    <li>
        <b>Early 1981</b>
        : FSLN becomes more radical as moderates are kicked off of the governing junta. Eventually, Arturo Cruz and &#201;den Pastora became disaffected with
        the revolution and defected. (Nicaragua)
    </li>
    <li>
        <b>March 9, 1981</b>
        : Reagan authorizes the CIA to help interdict arms trafficking to El Salvador, but this does not give them the power to arm rebel groups. Concurrently,
        the administration tries to convince the FSLN to stop their activities. (Nicaragua)
    </li>
    <li>
        <b>December 1, 1981</b>
        : Reagan signs the order allowing the CIA to support the contras with arms, equipment, and money in order to put pressure on the regime. This is
        concurrent with U.S. efforts at diplomatic initiatives and the strengthening of pro-U.S. regimes in Central America. Covert activities are viewed as
        the best way to pressure the regime. The Contras are trained by Argentineans and operated out of Honduras. (Nicaragua)
    </li>
    <li>
        <b>March 14, 1982</b>
        : Opposition forces blow up 2 bridges, including one used to ship supplies to Salvadorian rebels. The Sandinistas impose state of emergency and crack
        down on the opposition while tightening press restrictions. (Nicaragua)
    </li>
    <li>
        <b>April 1982</b>
        : Pastora declares that he is going to take up arms against the regime. In May, Robelo declares his support and soldiers leave the Sandinistan army to
        join Pastora. (Nicaragua)
    </li>
    <li>
        <b>June 1982</b>
        : The birth of the Reagan Doctrine is publicly announced. This is Reagan&#8217;s policy of supporting democratization everywhere. Thus, the goal of
        covert operations in Nicaragua shifts from one of ostensibly interdicting arms to supporting a change in government. (Nicaragua)
    </li>
    <li>
        <b>December 7, 1982</b>
        : The CIA introduces the &#8220;political directorate of the FDN;&#8221; this included Edgar Chamorro, Adolfo Calero, and Enrique Berm&#250;dez, a
        former guardsman. Chamorro later resigned due to the fact that, in his view, contras purposely targeted civilians. (Nicaragua)
    </li>
    <li>
        <b>December 21, 1982</b>
        : The first Boland Amendment is passed into law, which barrs &#8220;the use of funds &#8216;for the purpose of&#8217; overthrowing the government of
        Nicaragua or provoking a war between Nicaragua and Honduras. (Nicaragua)
    </li>
    <li>
        <b>1983</b>
        : U.S. actively engaged in preventing arms sales to Iran. (Iran)
    </li>
    <li>
        <b>1983</b>
        : Adnan Khashoggi first meets with Robert McFarlane, Theodore Shackley meets Iranians Manucher Hashemi, Manucher Ghorbanifar and Hassan Karoubi. (Iran)
    </li>
    <li>
        <b>1982-1983</b>
        : Some in Congress, especially liberal Democrats, are angry at the continued support of the Contras and the growing war in Nicaragua. They think that
        it might violate the Boland Amendment. (Nicaragua)
    </li>
    <li>
        <b>Spring 1983</b>
        : McFarlane thinks that Israel could give some of its U.S. appropriations to the Contras. (Nicaragua)
    </li>
    <li>
        <b>Late 1983</b>
        : The CIA assists the contras in attacking transportation and economic targets in Nicaragua. (Nicaragua)
    </li>
    <li>
        <b>1984</b>
        : Early in the year: Ghorbanifar and Khashoggi meet in Hamburg and devise skeleton of plan that will become Iran arms deal. (Nicaragua)
    </li>
    <li>
        <b>January 1984</b>
        : McFarlane formally requests the NSC to formally examine ways to influence Iran. Report conveys an impasse. (Iran)
    </li>
    <li>
        <b>January 1984</b>
        : A retired Army general, John Singlaub, offers to help the contras get money, supplies, and recruit military advisers; he continuously meets with
        North and gets his approval for his actions. Singlaub tires to get money from Taiwan and South Korea for the Contras. Eventually, North and Richard
        Secord stop him from doing further business for the contras. (Nicaragua)
    </li>
    <li>
        <b>January 7, 1984 </b>
        and <b>February 29, 1984</b>: The CIA mines harbors in Nicaragua. Oliver North knew of the plan and recommended that the President sign off on it.
        (Nicaragua)
    </li>
    <li>
        <b>February or March 1984</b>
        : McFarlane meets with his Israeli counterpart David Kimche and asked him if Israel would help train the Contras. (Nicaragua)
    </li>
    <li>
        <b>March 1984:</b>
        Religious fundamentalist group Islamic Holy War kidnaps William F. Buckley, the CIA chief in Beirut, Lebanon. More American hostages taken in the
        following years. (Iran)
    </li>
    <li>
        <b>March 27, 1984</b>
        : Casey says that McFarlane should look for funding from Israel and others; Israel declined, and Shultz at first questioned the legality of getting
        money from other countries. (Nicaragua)
    </li>
    <li>
        <b>Spring 1984</b>
        : Congress finds out about the CIA&#8217;s mining of the harbors, which Casey had barely informed the intelligence committees about. There was great
        uproar and backlash against aid to the contras. (Nicaragua)
    </li>
    <li>
        <b>June 1984</b>
        : McFarlane gets money from Saudi Arabia for the Contras; he later stated that he had not solicited it but had merely mentioned the problem of losing
        aid for the Contras, and the Saudis gave money. (Nicaragua)
    </li>
    <li>
        <b>Summer 1984</b>
        : North asks Richard Secord to get involved with the contras by getting supplies to them. He and Albert Hakim join the cause. They strike a deal with
        Calero to supply arms. Israeli representatives drawn into discussion (Nicaragua)
    </li>
    <li>
        <b>1984</b>
        : Some in the Administration have the idea of having someone set up a private tax-exempt organization to raise money for the Contras. Carl Channell
        spearheads this effort to solicit private funds for the Contras. He would seek out potential donors, many of whom would meet with North or Reagan.
        (Nicaragua)
    </li>
    <li>
        <b>August 1, 1984</b>
        : House passes the second Boland Amendment, which reads &#8220;During fiscal year 1985, no funds available to the Central Intelligence Agency, the
        Department of Defense, or any other agency or entity of the United States involved in intelligence activities may be obligated or expended for the
        purpose or which would have the effect of supporting directly or indirectly, military or paramilitary operations in Nicaragua by any nation, group,
        organization, movement or individual.&#8221; Boland states that U.S. support for the contras gave the FSLN an excuse to be repressive and undemocratic.
        (Nicaragua)
    </li>
    <li>
        <b>October 12, 1984</b>
        : Boland II becomes law. Two ways for the administration to potentially bypass the act are to solicit third-party funds (private donors/third
        countries) or use the NSC on the logic that it was not covered under Boland. Oliver North, on loan to the NSC from the Marine Corps, spearheads this
        activity. (Nicaragua)
    </li>
    <li>
        <b>November 4, 1984</b>
        : Nicaraguan elections are held; Daniel Ortega, Nicaragua&#8217;s president, wins. (Nicaragua)
    </li>
    <li>
        <b>November-December 1984</b>
        : North gives intelligence to the contras regarding Soviet helicopters sold to the Sandinistas. (Nicaragua)
    </li>
    <li>
        <b>1985</b>
        : Channell&#8217;s organizations take in over $12 million, of which $2.7 million went to the Contras. (Nicaragua)Ghorbanifar and Khashoggi meet in
        Hamburg and devise skeleton of plan that will become Iran arms deal. (Iran)
    </li>
    <li>
        <b>February 1985</b>
        : When the initial Saudi contribution runs out, they donate more. In total, they gave $32 million. (Nicaragua)
    </li>
    <li>
        <b>1985</b>
        : North helps to get a $1 million contribution for the contras from Taiwan. (Nicaragua)
    </li>
    <li>
        <b>May 1, 1985</b>
        : Reagan announces an economic and trade embargo against Nicaragua due to a trip Ortega took to Moscow. (Nicaragua)
    </li>
    <li>
        <b>Summer 1985</b>
        : Secord and Hakim become Calero&#8217;s sole arms suppliers. In all, they sell about $11 million in arms and other equipment to the contras.
        (Nicaragua) Israeli representatives drawn into discussion (Iran)
    </li>
    <li>
        <b>June 1985</b>
        : Cruz, Robelo, and Calero unite as the UNO (United Nicaraguan Opposition). (Nicaragua)
    </li>
    <li>
        <b>July 1985</b>
        : North approaches Secord about conducting air supply operations for the contras. (Nicaragua)
    </li>
    <li>
        <b>July 1, 1985</b>
        : President Reagan publicly denounces bartering with terrorists. (Iran)
    </li>
    <li>
        <b>July 3, 1985</b>
        : McFarlane meets with Israeli David Kimche, who is in the U.S. on behalf of the Israelis who had met with Khashoggi and Ghorbanifar. The
        arms-for-hostages deal is first outlined, as is the prospect of improving the U.S.-Iran relationship. (Iran)
    </li>
    <li>
        <b>July 16, 1985</b>
        : McFarlane meets with Reagan and his Chief of Staff Regan while Reagan is in the hospital recovering from surgery. They discuss the possibility of
        selling arms to Iran via Israeli in order to get the release of the hostages and to open communications with Iran. The details of this visit are hazy,
        but McFarlane came away from it with the idea that the President had encouraged him to go forward with discussions with the Iranians and Israelis.
        (Iran)
    </li>
    <li>
        <b>August 1985</b>
        : Reagan approves the plan to allow Israeli to sell U.S.-made weapons to Iran. (Iran)
    </li>
    <li>
        President Reagan signs into law legislation giving $14 million in humanitarian assistance directly to the contras. (Nicaragua)
    </li>
    <li>
        <b>August 20, 1985</b>
        : first load of missiles sent from Israeli to Iran. (Iran)
    </li>
    <li>
        <b>September 15, 1985</b>
        : American hostage Benjamin Weir released. Colonel Oliver North brought in to deal with logistics. (Iran)
    </li>
    <li>
        <b>November 1985</b>
        : second load of missiles sold. Major General Richard Secord brought in to help replenish Israeli&#8217;s supply of weapons. (Iran)
    </li>
    <li>
        First funds from arms sales diverted to Nicaraguan Contras through Secord&#8217;s Enterprise. (Nicaragua)
    </li>
    <li>
        <b>Late 1985/Early 1986</b>
        : Boland language is loosened in the 1986 intelligence authorization bill to say that the CIA can provide training and intelligence to the Contras as
        long as it does not &#8220;amount to participation in the planning for execution of military or paramilitary operations&#8221; or participation in
        &#8220;logistics activities integral to such operations.&#8221; New legislation also allows the Administration to get funds from third countries and
        private parties provided there is no quid pro quo. (Nicaragua)
    </li>
    <li>
        <b>January 17, 1986</b>
        : President Reagan signs a Presidential Finding authorizing the transfer of arms to Iran through the Enterprise in order to release the U.S. from
        liability. (Iran)
    </li>
    <li>
        <b>February 1986</b>
        : Taiwan gives another $1 million. North, Ghorbanifar and Iranian representative meet in Germany, work out a plan to send further arms in exchange for
        the release of U.S. hostages. North, Ghorbanifar and Iranian representative meet in Germany, work out a plan to send further arms in exchange for the
        release of U.S. hostages. (Iran)
    </li>
    <li>
        <b>February 27, 1986</b>
        : US sent 1,000 TOWs to Iran, no hostages released. (Iran)
    </li>
    <li>
        <b>March 7, 1986</b>
        : Ghorbanifar allegedly suggests the diversion to Cave (Nicaragua)
    </li>
    <li>
        <b>April 4, 1986</b>
        : Oliver North writes the &#8220;Diversion Memorandum&#8221; which clearly lays out what is going on with the transfer of funds to Contras. (Iran)
    </li>
    <li>
        <b>May/June 1986</b>
        : Only $1.2 million+ of as much as $11 million in the enterprise&#8217;s accounts was spent on the Contras. (Nicaragua)
    </li>
    <li>
        <b>August 1986</b>
        : Secord and his business partner Albert Hakim meet with a new Iranian contact, Ali Hashemi Bahramani, and open up a second channel in Iran. (Iran)
    </li>
    <li>
        <b>Summer 1986</b>
        : Congress passes $100 million in aid to the contras. At this point, most in Congress were tired of the Sandinistas. (Nicaragua)
    </li>
    <li>
        <b>October 5, 1986</b>
        : A plane flying supplies to the Contras was shot down in Nicaragua and Eugene Hasenfus was captured; it seemed the USG was involved but Reagan denied
        it and Congress didn&#8217;t look into it too thoroughly. This eventually led to the full exposure of the operation and the Iran-Contra affairs
        (Nicaragua)
    </li>
    <li>
        <b>October 1986</b>
        : North meets with this new group of Iranians, and Hakim, serving as a U.S. representative, works out a nine-point plan with the group. (Iran)
    </li>
    <li>
        <b>October 28, 1986</b>
        : first shipment through second channel sent to Iran. Iran paid $3.6 million to the Enterprise, of which $2 million was turned over to the CIA, who had
        official supplied the weapons. The remainder was diverted to the Contras. (Iran)
    </li>
    <li>
        <b>November 1986</b>
        : two Lebanese newspapers break story of arms deal, arms deal comes to an end. (Iran)
    </li>
</ul>
