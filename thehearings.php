    <p>On May 5, 1987, the world tuned in to watch the joint hearings of House Select Committee to Investigate Covert Arms Transactions with Iran and the Senate Select Committee on Secret Military Assistance to Iran and the Nicaraguan Opposition—soon to be known as the “Iran-Contra hearings.”

The hearings were called to order about two months after the release of the <a href="http://hdl.handle.net/2027/mdp.39015014946043" class="content_link">Tower Commission’s Report</a> that was ordered by President Ronald Reagan, which did not blame Reagan directly for the Iran-Contra Affairs, but said he should have been more informed about the events. </p>
    <p>Sen. Daniel Inouye (D-HI), the chair of the Senate Select Committee, called the hearing to order by saying he hoped that the facts of the “sad affair” would be revealed.  Rep. Lee Hamilton (D-IN), the chair of the House Select Committee, predicted that the hearings would prove that “many things went wrong.”  Sen. Warren Rudman (R-NH), vice chair of the Senate committee, asked if the “foreign policy process” was “flawed” or “simply ignored.”  Rep. Dick Cheney (R-WY), the ranking Republican on the House committee, largely defended President Ronald Reagan and the power of the Executive Branch.      </p>
    <p>With those opening statements, the first of 41 incisive, fascinating days of hearings began.  For three months, television cameras would capture all the action.  On this site, we feature about 100 clips of testimony involving 13 key figures as well as several members of the special congressional committees formed to investigate the affair. </p>
