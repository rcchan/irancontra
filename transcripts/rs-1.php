I want to ask you some questions about where that money went.
Before I do, let me ask you this. Up until a few days ago, did you
have access to the records necessary to determine where that
money went?
<br/><br/><strong>Mr. SECORD</strong>. No.
<br/><br/><strong>Mr. NIELDS</strong>. Who did?
<br/><br/><strong>Mr. SECORD</strong>. The committee, the House and Senate Select Committee had these records which they received recently from Mr.
Albert Hakim.
