<br/><br/><strong>Mr. NIELDS</strong>. And what was the total amendment of disbursements that you identified from February 1986.
<br/><br/><strong>Mr. SECORD</strong>. Between $5 and $6 million.
<br/><br/><strong>Mr. NIELDS</strong>. And did you then try to determine what other
sources of funds those disbursements might have come from?
<br/><br/><strong>Mr. SECORD</strong>. Yes. And we located donations that came in during
the timeframe for the Contra projects of $1.649 million. In addition
to that, there were in the accounts in February of 1986 some
$470,000. So if you total those two numbers, you have about $2 million.
<br/><br/><strong>Mr. NIELDS</strong>. And you have assumed all of that $2 million was
spent on Contra projects?
<br/><br/><strong>Mr. SECORD</strong>. It was.
<br/><br/><strong>Mr. NIELDS</strong>. And then the remaining amount of money that was
spent on Contra projects, I take it, had to come out of the proceeds
of the arms sales to Iran.
<br/><br/><strong>Mr. SECORD</strong>. That is the method we used; that is correct.
<br/><br/><strong>Mr. NIELDS</strong>. Can you give us a little bit more detail on the subject of just what kinds of expenditures for the benefit of the Contras were made out of these bank accounts?
<br/><br/><strong>Mr. SECORD</strong>. The airlift project had three components to it. The
procurement and the operations of the aircraft, of course. That
meant two C-123 K transport aircraft, two Caribou aircraft, and
one light utility airplane, a Maule, so there were five aircraft to
support it.
We also were required to construct an emergency landing field in
one of the Central American countries. That was the second component, and the third component was the procurement of limited
quantities of munitions, light infantry munitions for the purpose of
troops, air drops, to the southern Contras, the Contras located in
the southern part of Nicaragua.
<br/><br/><strong>Mr. NIELDS</strong>. Did you have to spend money on salaries in addition?
<br/><br/><strong>Mr. SECORD</strong>. Yes, of course. To support the airlift operation we
spent money on salary, fuels, spare parts, on maintenance, on support for the air crews.
<br/><br/><strong>Mr. NIELDS</strong>. Was there any money paid to Contra leaders?
<br/><br/><strong>Mr. SECORD</strong>. Yes.
<br/><br/><strong>Mr. NIELDS</strong>. Who and how much?
<br/><br/><strong>Mr. SECORD</strong>. There was a monthly disbursement of $10,000 a
month to one Contra leader and $5,000 a month to another. That is
Robelo and Cruz, and on several occasions we made disbursements
to Mr. Adolfo Calero's account with the FDN, the main body of the
Contras.
<br/><br/><strong>Mr. NIELDS</strong>. During this period of time were there any payments
directly to Mr. Calero, past February 1986?
<br/><br/><strong>Mr. SECORD</strong>. I believe there were some. I would have to look at
the records to refresh my memory on that point, but I think there
was $200,000 disbursed during that period for the FDN contras.
<br/><br/><strong>Mr. NIELDS</strong>. In any event, your memory is there was $200,000 at
some point in time, and you would be willing to refresh your
memory on that question by further consultation with the records?
<br/><br/><strong>Mr. SECORD</strong>. Yes, sir.
