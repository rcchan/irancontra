<br/><br/><strong>Mr. NIELDS</strong>. I take it by your answer that means something just
a very little bit over $30 million?
<br/><br/><strong>Mr. SECORD</strong>. Yes.
<br/><br/><strong>Mr. NIELDS</strong>. So if you were speaking in round numbers, it would
be $30 million?
<br/><br/><strong>Mr. SECORD</strong>. Yes, sir.
<br/><br/><strong>Mr. NIELDS</strong>. How much money was paid, to your knowledge,
either directly or indirectly to the U.S. Treasury out of that
money?
<br/><br/><strong>Mr. SECORD</strong>. I believe it was about $12.3 million.
<br/><br/><strong>Mr. NIELDS</strong>. So in round numbers, $12 million?
<br/><br/><strong>Mr. SECORD</strong>. $12 million.
<br/><br/><strong>Mr. NIELDS</strong>. I want to ask you some questions about, I think you
