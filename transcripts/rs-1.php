yours?
<br/><br/><strong>Mr. SECORD</strong>. Yes, he was.
<br/><br/><strong>Mr. NIELDS</strong>. How did the donors know where to put the money,
to your knowledge.
<br/><br/><strong>Mr. SECORD</strong>. I gave the account name and number to Colonel
North and he, in turn gave it to whomever was interested.
<br/><br/><strong>Mr. NIELDS</strong>. I take it then, Mr. Secord, that at the time that you
