containing money for the benefit of the Contras.
<br/><br/><strong>Mr. NIELDS</strong>. And that bank account was Lake Resources?
<br/><br/><strong>Mr. SECORD</strong>. Yes.
<br/><br/><strong>Mr. NIELDS</strong>. At a later point in time, February of 1986, and forward, were the proceeds of arms sales to Iran also put into the
