<br/><br/><strong>Mr. SECORD</strong>. The money was coming from donated funds and
these funds were coming, as I understand it, from private individuals and from some friendly countries.
<br/><br/><strong>Mr. NIELDS</strong>. And where were these moneys being put?
<br/><br/><strong>Mr. SECORD</strong>. In November of 1985 they were being deposited in a
