the end of the year, ultimately, and that was a substantial expense.
The procurement cost was about $350,000, and there were monthly operational costs. We have not yet audited those accounts.
<br/><br/><strong>Mr. NIELDS</strong>. And when you said there was a requirement, was
the ship bought for private purposes or was this ship bought for
governmental purposes?
<br/><br/><strong>Mr. SECORD</strong>. This ship was bought for, initially, to be used on another Government project which is not related to Iran.
<br/><br/><strong>Mr. NIELDS</strong>. Was it related to Nicaragua?
<br/><br/><strong>Mr. SECORD</strong>. No.
<br/><br/><strong>Mr. NIELDS</strong>. And at whose request was the ship purchased?
<br/><br/><strong>Mr. SECORD</strong>. I received the request from Colonel North.
<br/><br/><strong>Mr. NIELDS</strong>. Were there any other projects or purposes for which
