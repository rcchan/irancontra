<strong>Chairman HAMILTON</strong>: Your opening statement made the analogy to a
baseball game. You said the playing field here was uneven and the
Congress would declare itself the winner. I understand your sentiments, but may I suggest that we are not engaged in a game with
winners and losers. That approach, if I may say so, is self-serving
and ultimately self-defeating. We all lost.
The interests of the United States have been damaged by what
happened. This country cannot be run effectively when major foreign policies are formulated by only a few, and are made and carried out in secret, and when public officials lie to other nations and
to each other.<br /><br />
One purpose of these hearings is to change that. The self-cleansing process, the Tower Commission, and these joint hearings, and
the report which will follow, are all part, we hope, of a process to
reinvigorate and restore our system of government.
I don't have any doubt at all, Colonel North, that you are a patriot. There are many patriots in this country, fortunately, and many
forms of patriotism. For you, perhaps patriotism rested in the conduct of deeds, some requiring great personal courage, to free hostages and to fight communism. And those of us who pursue public
service with less risk to our physical well-being admire such courage.
But there's another form of patriotism which is unique to democracy. It resides in those who have a deep respect for the rule of law
and faith in America's democratic traditions. To uphold our Constitution requires not the exceptional efforts of the few but the confidence and the trust and the work of the many.<br /><br />
Democracy has its frustrations. You've experienced some of
them, but we—you and I— know of no better system of government, and when that democratic process is subverted, we risk all
that we cherish. </font></div>
