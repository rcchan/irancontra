<p>Mr. RUDMAN.</p><p>
By the time these hearings are concluded, the American people
will learn the answers to the five final questions: who, what, when,
why, and how.</p><p>
Of course, we will examine the role of the President and various
executive branch officials. What actions did the President specifically approve of? What exactly did Federal officials do? On whose
authority was such actions taken, and were any laws violated?</p><p>
We will also examine how the various aspects of the affair developed. Did various people consciously set out to violate the law?</p><p>
Were they well-intentioned policies and motives which went
astray? Was the problem attributable in any way to unclear or
vague Federal laws on the issue?</p><p>
Finally, we must look at how the foreign policy process worked.
Is the existing process flawed, or was it simply ignored? What are
the ramifications when foreign policy is privatized in a way that
leaves it susceptible to the control of profiteers rather than policymakers?
</p><p>The story that will be told is a sad one. There will be evidence of
illegal behavior and contempt for our democratic form of government. There will be stories of greed and incompetence. There are
many victims, especially the American people, who have a right to
expect better from their Government. While the investigation is
still underway, we already have sufficient evidence to establish
that this is an inexcusable fiasco of the first order.</p><p>
It is important, however, to keep things in perspective. These
hearings, while laying out an unfortunate affair, will also serve as
a reminder of the fundamental strength of the American system.</p><p>
This investigation and these hearings demonstrate the self-corrective nature of our democratic government. They prove once again
the brilliance of Winston Churchill when he stated that "democracy is the worse system devised by the wit of man, except for all the
others." </p>


