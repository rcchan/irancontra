<br/><br/><strong>Mr. LIMAN</strong>: Is it a fact that the first time you met him was the
end of February?
<br/><br/><strong>Mr. HAKIM</strong>: Yes.
<br/><br/><strong>Mr. LIMAN</strong>: Is it a fact that you had very little personal contact
