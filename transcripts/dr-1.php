<strong>Mr. SMILJANICH</strong>: When did you first discover that the President
had signed a Finding on January 17 allowing this matter to go forward?
<br/><br/><strong>Mr. REGAN</strong>: Sometime in October of 1986. Bill Casey called me to
ask if I had a copy of the Finding of January. I asked my staff to
see if we had a copy in our files. We had none.
I went back to him and said no, I don't have one, ask John Poindexter. I said how come you don't have one. He said we don't have
one in our files, and he said that's why I'm trying to find one.
So I didn't realize that the thing had been signed or where it was
until late October of '86.
<br/><br/><strong>Mr. SMILJANICH</strong>: So you are saying that in late October of 1986,
after this operation had been going forward for 8, 9, 10 months,
neither you nor Director Casey had specific knowledge that the
President had signed a Finding dated January 17?
<br/><br/><strong>Mr. REGAN</strong>: I think both of us—although I shouldn't characterize
what he was thinking, but I will say that I was thinking that all
along I just assumed that he probably had signed it. I just didn't
see it.
<br/><br/><strong>Mr. SMILJANICH</strong>: And you couldn't locate a copy and the Director
of Central Intelligence couldn't locate a copy?
<br/><br/><strong>Mr. REGAN</strong>: That is correct.
<br/><br/><strong>Mr. SMILJANICH</strong>: You finally did determine who had the only
copy of that Finding; is that right?
<br/><br/><strong>Mr. REGAN</strong>: Yes.
<br/><br/><strong>Mr. SMILJANICH</strong>: Who did you call?
<br/><br/><strong>Mr. REGAN</strong>: I asked Poindexter about it the next day. I said did
Casey call you about that Finding. He said yes. I said where the
hell has it been—excuse my language. That is normal for me.
Excuse me.
And he said I have the only copy, it's in my safe, he said it's with
me.
<br/><br/><strong>Mr. SMILJANICH</strong>: Moving back to the January-February timeframe, in late February, you were—you and the President were
aware that 1,000 TOW missiles had been shipped to Iran from the
United States?
<br/><br/><strong>Mr. REGAN</strong>: Yes.
<br/><br/><strong>Mr. SMILJANICH</strong>: The President knew of and approved those shipments?
<br/><br/><strong>Mr. REGAN</strong>: Yes.
<br/><br/><strong>Mr. SMILJANICH</strong>: And under the plan that had been discussed
with the senior advisers in January, upon the shipment of this first
1,000 TOW missiles all of the American hostages held in Lebanon
were supposed to be released; isn't that correct?
<br/><br/><strong>Mr. REGAN</strong>: Yes.
<br/><br/><strong>Mr. SMILJANICH</strong>: When no hostages were released after shipment
of those TOW missiles, what did you recommend the President do
about the fact that the Iranians had broken their word?
<br/><br/><strong>Mr. REGAN</strong>: I told him I thought we ought to break it off, that we
have been snookered again, how many times do we put up with
this rug merchant type of stuff—or words to that effect.
<br/><br/><strong>Mr. SMILJANICH</strong>: What did the—what was the President's attitude or decision?
<br/><br/><strong>Mr. REGAN</strong>: I think he shared my view that we had been had.
<br/><br/><strong>Mr. SMILJANICH</strong>: Did he instruct anyone to terminate their activities?
<br/><br/><strong>Mr. REGAN</strong>: No. There was a pause then and I sort of lost track
of what was going on. At that point we were deep in the middle of
the tax bill and the budget battle, and I sort of lost track of, you
know, what was going on. I wasn't paying that much attention to
it.
<br/><br/><strong>Mr. SMILJANICH</strong>: Well, it popped back up during the Tokyo
summit in May of 1986 when Secretary Shultz came to you and
was very concerned about something.
Do you recall that?
<br/><br/><strong>Mr. REGAN</strong>: Yes.
<br/><br/><strong>Mr. SMILJANICH</strong>: And what do you recall him telling you at that
time?
<br/><br/><strong>Mr. REGAN</strong>: Has the man's name been mentioned in open hearing? It has?
He told me that Ambassador Price had called him to alert him to
the fact that Tiny Rowlands, a British business person, was inquiring about whether or not we were engaged in shipping arms to
Iran, that he was being offered a—I suppose you'd say a share in
the underwriting of this, and wanted to know was this indeed U.S.
policy.
And when I heard the name Rowlands involved in this, I was
really concerned.
<br/><br/><strong>Mr. SMILJANICH</strong>: You knew of Mr. Rowlands?
<br/><br/><strong>Mr. REGAN</strong>: By reputation.
<br/><br/><strong>Mr. SMILJANICH</strong>: What was your concern at that time?
<br/><br/><strong>Mr. REGAN</strong>: Well, you know, what kind of a deal is this? Here we.
are going to outsiders, to a "British merchant banker," or entrepreneur, as part of the Iranian arms sales? I couldn't follow what was
going on.
So I told him, I said you better get a hold of Poindexter and find
out what is going on there. So he said he would. So that's the last I
heard of that one.
<br/><br/><strong>Mr. SMILJANICH</strong>: Well, Secretary Shultz has testified that it was
his recollection that when he gave you this information in Tokyo,
first of all that you were alarmed.
Would it be fair to say you were alarmed at this information?
<br/><br/><strong>Mr. REGAN</strong>: Concerned, yes.
<br/><br/><strong>Mr. SMILJANICH</strong>: It was Secretary Shultz's recollection that you
stated to him that you would attempt to bring it to the President's
attention.
<br/><br/><strong>Mr. REGAN</strong>: I don't recall that. I'm not sure that I thought that
of sufficient importance to involve the President.
<br/><br/><strong>Mr. SMILJANICH</strong>: Well, if you were alarmed and you felt that this
matter was possibly now in the hands of some people that you
didn't particularly trust, why didn't you feel that this was something that—particularly given the fact that after the February
shipment, when no hostages were released, you felt that this
matter ought to be terminated?
Why didn't you bring it to the President's attention at that
point?
<br/><br/><strong>Mr. REGAN</strong>: Well, having—knowing that the Secretary of State
was aware of this information, having made my concerns known to
the Secretary of State, and he in turn reflecting those views to the
National Security Adviser, I felt that those were two very competent people who could handle the situation and would.
There was no need for me to be going either around them or
bringing something directly to the President that was really in
their purview. I was trying to keep channels, if you will, or keep
these things in channels.
<br/><br/><strong>Mr. SMILJANICH</strong>: Did you ever talk to the President about this
matter? I mean around that timeframe.
<br/><br/><strong>Mr. REGAN</strong>: Around that time, no.
<br/><br/><strong>Mr. SMILJANICH</strong>: Did you check back with Secretary Shultz at
any point to see whether he had been satisfied by Admiral Poindexter's—
<br/><br/><strong>Mr. REGAN</strong>: No. Again, you've got to remember we were at an
economic summit. I was concerned that the President was being
briefed, had enough information, going over with him some of the
fine points of what the heads of government were discussing. So I
put this sort of thing out of my mind.
<br/><br/><strong>Mr. SMILJANICH</strong>: Now, in late May of 1986, the President—after
Mr. McFarlane's trip to Tehran, the President turned down a proposition that had been made to Mr. McFarlane for the delivery of
two hostages for the HAWK spare parts that were being shipped
through Israel.
The President and Mr. McFarlane had insisted that all the hostages be released before any more parts would be delivered; isn't
that correct?
<br/><br/><strong>Mr. REGAN</strong>: That's right.
<br/><br/><strong>Mr. SMILJANICH</strong>: All right.
By November of 1986 the President had authorized the delivery
of all of the HAWK parts, 500 additional TOW missiles and two
pallets of medicine, all in return for those same two hostages.
Why did our negotiating position collapse so completely during
that timeframe?
<br/><br/><strong>Mr. REGAN</strong>: I cannot offer you an explanation of that. I don't
know.
<br/><br/><strong>Mr. SMILJANICH</strong>: Well, as someone who came to government service from a phenomenal career on Wall Street, it must have occurred to you that the United States was being hustled?
<br/><br/><strong>Mr. REGAN</strong>: It was a bait and switch.
<br/><br/><strong>Mr. SMILJANICH</strong>: Did you so advise the President?
<br/><br/><strong>Mr. REGAN</strong>: Yes.
<br/><br/><strong>Mr. SMILJANICH</strong>: And what did the President say about why he
was allowing this thing to continue?
<br/><br/><strong>Mr. REGAN</strong>: Well, I think he assumed that there are many times
when a political leader has to deal with unsavory characters in
order to accomplish an end, and I think he was under the impression, well, if we can get to the top there, someplace toward the top
in Iran, perhaps, you know, we can deal with them, but we have to
work our way up the ladder. 

