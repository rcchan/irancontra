<strong>Mr. LIMAN</strong>: Do you recall who was present when the President
signed the Finding?
<br/><br/><strong>Mr. POINDEXTER</strong>: No, I don't. One of the things that I think my
recollection is very poor on are the circumstances of the President
actually signing this. I recall that was a day or so after Mr. McFarlane
had resigned, and the President had just, I am not even-I
guess we had announced it on the 4th.<br/>
Mr. McFarlane actually resigned on the 30th of November, we
announced it on the 4th of December, and my recollection is that
he signed this the following day, on the 5th.<br/>
My recollection now is that the CIA, especially the Deputy Director,
John McMahon, was very anxious to get this signed. I frankly
was never happy with it because it was not fully staffed, and I
frankly can't recall when I showed it to the President whether—who
was there or exactly what the discussion was or even what I
recommended to him at this point.<br/>
I simply can't remember that.
<br/><br/><strong>Mr. LIMAN</strong>: But you do recall that whatever you recommended,
the President read it and he signed it?
<br/><br/><strong>Mr. POINDEXTER</strong>: Yes, he did. He did sign it.
<br/><br/><strong>Mr. LIMAN</strong>: And there was, in fact, the recommendation from
Bill Casey that he sign it and Bill Casey was a person whose
advice the President valued?
<br/><br/><strong>Mr. POINDEXTER</strong>: He did.
<br/><br/><strong>Mr. LIMAN</strong>: What happened to that Finding?
<br/><br/><strong>Mr. POINDEXTER</strong>: As I said earlier, I destroyed that by tearing it
up on the 21st of November, because I thought it was a significant
political embarrassment to the President, and I wanted to protect
him from possible disclosure of this.
To get into the details of exactly how it happened, which I
assume you are interested in--
<br/><br/><strong>Mr. LIMAN</strong>: When you say the 21st of November, you are talking
about the 21st of November 1986?
<br/><br/><strong>Mr. POINDEXTER</strong>: 1986; that is correct.
<br/><br/><strong>Mr. LIMAN</strong>: Now, would you tell the panel the circumstances of
your destroying this Finding because you thought it would be a significant
political embarrassment to the President?
<br/><br/><strong>Mr. POINDEXTER</strong>: I will. The Finding, the existence of the Finding
I had completely forgotten in early November, 1986. As I said
before, the Finding initially was prepared by the CIA for the
reason that I stated. I can recall in my time at the White House
one or possibly two other Findings that had a retroactive nature to
them. I, frankly, was always uncomfortable with that, because I
thought it didn't particularly make a lot of sense.<br/><br/>
The Finding was very narrow. It was prepared before there had
been thorough discussion of the issue. As I said ear her, I came into
the issue in a full, responsible way in early December of 1985.<br/><br/>
Prior to that time, Mr. McFarlane handled it. I felt that it was important
that we improve on this Finding so that we clearly layout
what the objectives were in the Iranian initiative. And, as we'll get
to, I am sure, there were other Findings in January.
But, anyway, after this Finding was signed, it was retained in my
immediate office, and at some point after it was signed, I had apparently
given it to Commander Thompson, my military assistant,
to put in an envelope in his safe to keep. I had, as I said, completely
forgotten about it.<br/><br/>
On November, the 21st, when Ed Meese called me and said-well
to go back a step, we had run into a problem in November of what
had actually happened in 1985. It was very dim in people's memories.
We didn't think we had much in writing. As I think you have
heard Colonel North testify, we, frankly, did not realize the old
PROF notes existed. My policy was to erase them, and I apparently
did it the right way, and I don't think Colonel North did it the
right way. So we didn't have the benefit that these committees
have in going back over these old PROF notes, or we didn't realize
that we had that opportunity.<br/><br/>
But Ed Meese and I had talked many times during the month of
November, and when it became clear that there was a disagreement
between Cabinet-level officials as to what had happened in
November of 1985, he indicated that he wanted to come over and
ask the President to have a factfinding session, primarily with the
Cabinet-level officials involved, to try to sort out what had happened,
actually happened, in November of 1985.<br/><br/>
And he called me early in the morning on the 21st of November
and told me this, and he said he had an appointment to see the
President at 11:30, and he wanted me and Don Regan to go with
him, which we did, at 11:30. He told the President about the controversy-
not really controversy-the different recollections as to
what had happened in November and said he thought it would be
useful if he would have a couple of his people that were close to
him look into the matter to see if they could piece together what
had happened. The President readily agreed, as I did, at that point.
Because here we had Mr. McFarlane on one hand and Secretary
Shultz on the other hand recalling different recollections as to
what had happened in the early days of November of 1985.<br/><br/>
So Ed called me after lunch, as I recall, and said he was going to
send over a couple of his people either that afternoon or the next
day, and I am not sure which, and he asked if I would have the
appropriate documents pulled together so they could take a look at
them. I said I would do that.<br/><br/>
After he called, I called Commander Thompson, my military assistant,
and asked him to take charge of pulling these documents
together, and then I called Colonel North and told him of my conversation
with Mr. Meese and asked him to cooperate with Commander
Thompson and Mr. Meese's people.<br/><br/>
I called Colonel North because he was very protective of the documents
that he had, and I wanted to make sure that he understood
the tasking that I had given Commander Thompson.
Later in the afternoon or early evening, Commander Thompson
brought III to my office the envelopes that I had given him earlier
containing the material we had on the Iranian project in the immediate office, which was essentially the various Findings, and he
pulled out this November Finding, it was actually signed in December, and my recollection is that he said something to the effect that<br/><br/>
"They'll have a field day with this," or something to that effect
And my recollection is that the import of his comment was that
up until that time in November of 1986, the President was being
beaten about the head and shoulders, that this was—the whole Iranian project was just an arms-for-hostage deal.<br/><br/>
Well, this Finding, unfortunately, gave that same impression
And I, frankly, didn’t see any need for it at the time. I thought it
was politically embarrassing. And so I decided to tear it up, and 1
tore it up, put it in the burn basket behind my desk. I can't recall,
but I believe that Colonel North was there in the office, but I am a
little fuzzy on that point.
<br/><br/><strong>Mr. LIMAN</strong>: Was Commander Thompson there when you tore it
up?
<br/><br/><strong>Mr. POINDEXTER</strong>: I believe he was, but I can't swear to it. I know
he brought it in, and I can recall his comment, but exactly how
long it took—because when he made his comment, I said, "Well, let
me see the Finding," and he pulled it out and gave it to me, and I
read it, and at some point after that I tore it up, but it was within
a short period of time.
<br/><br/><strong>Mr. LIMAN</strong>: Now, admiral, you talked about the fact that you
were being—
[Witness conferring with counsel.]
<br/><br/><strong>Mr. LIMAN</strong>: Admiral, you talked about the fact that the President was being beaten around the head and shoulders by the media
for sanctioning an arms-for-hostage deal and that this Finding
seemed to corroborate it, and you, therefore, destroyed it in order
to prevent significant political embarrassment.
Did you regard one of the responsibilities of the National Security Adviser to protect the President from political embarrassment?
<br/><br/><strong>Mr. POINDEXTER</strong>: I think that it's always the responsibility of a
staff to protect their leader, and certainly in this case, where the
leader is the Commander in Chief, I feel very strongly that that's
one of the roles, and I don't mean that in any sense of covering up
But one has to always put things in the President's perspective and
to make sure that he is not put in a position that can be politically
embarrassing.
<br/><br/><strong>Mr. LIMAN</strong>: Now, admiral, a Finding represents a decision of the
President of the United States, correct?
<br/><br/><strong>Mr. POINDEXTER</strong>: It represents—
[Witness conferring with counsel.]
<br/><br/><strong>Mr. POINDEXTER</strong>: A Finding, I don't believe, is discussed in any
statute. It is discussed in various Presidential directives. It is an artifact of what the statute calls a Presidential determination.
<br/><br/><strong>Mr. LIMAN</strong>: And the President, when he signed this Finding, was
making a determination?
<br/><br/><strong>Mr. POINDEXTER</strong>: That is correct. But it's important to point out
that the Finding, that early Finding was designed for a very specific purpose, and was not fully staffed, and did not in any way ever
represent the total thinking on the subject.
<br/><br/><strong>Mr. LIMAN</strong>: Well, the President didn't authorize you to destroy
the Finding, correct?
<br/><br/><strong>Mr. POINDEXTER</strong>: He certainly did not.


