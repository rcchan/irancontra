Mr. CALERO<br/><br/>
Yes, we received funds from foreign sources. That was the only
way to survive after the U.S. aid was cut off by Congress. We gratefully welcomed the support of patriotic Americans and citizens of
other countries who understood our plight and the serious implications of our defeat. But we bore the brunt of fighting for our freedom. We never asked for intervention by American military forces.<br/><br/>
This has been our battle, and not a Vietnam.
We are making history but also repeating it. In 1775, front companies were set up in Europe to receive funds contributed by
France and Spain. It all developed into a delicate international
problem but it served a worthy cause, that of the American revolutionaries fighting to free this country from the British Empire.<br/><br/>
God bless all of the Americas. 


