<strong>Mr. NIELDS</strong>: Was the Attorney General aware in November of
1985 that 18 HAWK missiles had been shipped to Iran?
<br/><br/><strong>Mr. NORTH</strong>: I did not specifically address it to the Attorney General in November of 1985. I do remember discussions that included
the Attorney General subsequent to this event.
I believe there was one that Mr. McFarlane referred me to in December that I believe may well have addressed this issue because,
when he joined me in London, we talked about how to fix the problems that had been created by the September and November shipments.
One of the issues that had already come up by then was a draft
Finding prepared in concert with Mr. Sporkin, who was at the time
the general counsel of the Central Intelligence Agency. I was led to
believe, or at least came to believe, in 1985 that Mr. Sporkin had
gotten the acquiescence or support—either he or Director Casey—
of the Attorney General in that November Finding.
The November Finding specifically referred to prior actions.
<br/><br/><strong>Mr. NIELDS</strong>: Ratified them?
<br/><br/><strong>Mr. NORTH</strong>: Ratified prior actions.
<br/><br/><strong>Mr. NIELDS</strong>: Was that Finding ever signed?
<br/><br/><strong>Mr. NORTH</strong>: It is my understanding that the Finding was signed.
<br/><br/><strong>Mr. NIELDS</strong>: What is the basis for your understanding?
<br/><br/><strong>Mr. NORTH</strong>: I believe, although I do not recall specifically, but I
believe I saw a signed copy of that Finding.
<br/><br/><strong>Mr. NIELDS</strong>: Where?
<br/><br/><strong>Mr. NORTH</strong>: Admiral Poindexter's office.
<br/><br/><strong>Mr. NIELDS</strong>: Did you have a copy of it in your office?
<br/><br/><strong>Mr. NORTH</strong>: I did not. I had a draft copy.
<br/><br/><strong>Mr. NIELDS</strong>: When did you see the signed Finding?
<br/><br/><strong>Mr. NORTH</strong>: I think I may have seen a signed copy of it in early
December.
<br/><br/><strong>Mr. NIELDS</strong>: Of what year?
<br/><br/><strong>Mr. NORTH</strong>: 1986—sorry—'5.
<br/><br/><strong>Mr. NIELDS</strong>: That Finding referred only to arms and hostages;
isn't that true? It didn't refer to any broader purposes?
<br/><br/><strong>Mr. NORTH</strong>: Exactly. And that, as I indicated a few moments ago,
I perceived to be a serious deficiency in that Finding.
<br/><br/><strong>Mr. NIELDS</strong>: And a serious problem therefore with exposure of
the HAWK shipment?
<br/><br/><strong>Mr. NORTH</strong>: Exactly. The exposure of the HAWK shipment—
again, my priorities, safety of the hostages, safety of the second
channel, the international repercussions of a—what clearly in that
initial Finding sent to the National Security Adviser by Director
Casey was nothing more than an arms for hostages swap.
I would point out that Mr. Sporkin and I worked diligently over
the course of the next few days and weeks to prepare a Finding
which addressed what I thought were the broader issues and what
I clearly believe Mr. McFarlane believed to be the broader issues
and certainly Admiral Poindexter in support of the broader issues.
<br/><br/><strong>Mr. NIELDS</strong>: I want to return—we will come back to these subjects in a minute, Colonel North. But I would like to return to the
