First of all, you put some value, don't you, in the truth?
<br/><br/><strong>Mr. NORTH</strong>: I put great value in the truth. I came here to tell it.
<br/><br/><strong>Mr. NIELDS</strong>: So that was—that would be a reason not to put forward this version of the facts?
<br/><br/><strong>Mr. NORTH</strong>: The truth would be a reason not to put forward that
version of the facts. But, as I indicated to you a moment ago, I put
great value on the lives of the American hostages. I worked hard to
bring back as many as we could. I put great value in the possibility—that we could have ended the Iran-Iraq war.
<br/><br/><strong>Mr. NIELDS</strong>: We will get back to that in a minute.
Mr. SULLIVAN. Let him finish, counsel.
<br/><br/><strong>Mr. NORTH</strong>: —and we had established for the first time a direct
contact with people inside Iran who might be able to assist us in a
strategic reopening and who were at great risk if they were exposed.
And so, yes, I put great value in the truth. As I said, I came here
to tell it. But I also put great value on human life. And I put great
value on that second channel, who was at risk.
<br/><br/><strong>Mr. NIELDS</strong>: By putting out this false version of the facts, you
