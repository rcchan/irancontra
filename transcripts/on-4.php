there's a loose cannon on the gun deck of state at the NSC. That
wasn't what I heard while I worked there. I've only heard it since I
left. People used to walk up to me and tell me what a great job I
was doing.
The fact is there were many, many people, to include the former
Assistant to the President for National Security Affairs, the current National Security Adviser, the Attorney General of the
United States of America, the Director of Central Intelligence, all
of whom knew that to be wrong.
