version of the facts which wasn't true?
<br/><br/><strong>Mr. NORTH</strong>: Counsel, I think I have answered the question as
best I can. I am not too sure who you want to blame for committing who. If you want to blame me for committing others, that is
fine.
<br/><br/><strong>Mr. NIELDS</strong>: No one has suggested that, sir. My question to you is
