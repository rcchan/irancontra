forward.
Did Admiral Poindexter make that decision?
<br/><br/><strong>Mr. NORTH</strong>: I don't know. I did not ask Admiral Poindexter if he
had run this decision up the line.
<br/><br/><strong>Mr. NIELDS</strong>: Do you know it went at least as far as Admiral Poindexter?
<br/><br/><strong>Mr. NORTH</strong>: Oh, yes. I know that he had versions—not necessarily this one, but he had versions of this chronology which reflected
that kind of language, that is correct, that I had given him.
<br/><br/><strong>Mr. NIELDS</strong>: And did he ask you to put that language in?
<br/><br/><strong>Mr. NORTH</strong>: No. As I indicated earlier, I had gotten that language from Mr. McFarlane.
<br/><br/><strong>Mr. NIELDS</strong>: So you got the language from McFarlane. He persuaded you it was appropriate to put it in?
<br/><br/><strong>Mr. NORTH</strong>: Who is "he"?
<br/><br/><strong>Mr. NIELDS</strong>: McFarlane.
<br/><br/><strong>Mr. NORTH</strong>: Yes.
<br/><br/><strong>Mr. NIELDS</strong>: You put it in and you sent copies of the chronology
with that false version in it to Admiral Poindexter?
<br/><br/><strong>Mr. NORTH</strong>: And others.
I want to point out that I got back—as I was preparing—and
there were many, many versions
<br/><br/><strong>Mr. NIELDS</strong>: What—
<br/><br/><strong>Mr. NORTH</strong>: I got back in my office handwritten notes on the
marginalia on each of the documents, many of the documents,
notes from various people that were not recognizable to me, people
making editorial changes, if you will.
<br/><br/><strong>Mr. NIELDS</strong>: Did you discuss the wisdom of putting out a false
