<strong>Mr. LIMAN</strong>: Admiral, see if this part is correct, that the currency
for trying to get that influence that was being demanded, as reported by the Israelis, involved arms?
<br/><br/><strong>Mr. POINDEXTER</strong>: That is often the currency of any sort of business in the Middle East.
<br/><br/><strong>Mr. LIMAN</strong>: And in this case, that was the currency being demanded?
<br/><br/><strong>Mr. POINDEXTER</strong>: Yes. That is correct.
<br/><br/><strong>Mr. LIMAN</strong>: And it is also true that we did not want to authorize
arms shipments to the Iranians unless we were assured of getting
our hostages back; is that so?
[Counsel conferring with witness.]
<br/><br/><strong>Mr. POINDEXTER</strong>: As I was trying to lay out a moment ago, what
our concerns were, what our major objective was, the President
was clearly also concerned about the hostages. The President is a
very sensitive person, and he is concerned about individuals when
they are in difficulty. And so he, just as a human being, was concerned about the hostages.
I don't think that the President is overly concerned about them,
but he recognized that we did have an opportunity here to try to
get the hostages back, and there was no way that we could carry
on discussions with Iranian officials about broader objectives until
we got over the first obstacle and the first obstacle was to get the
hostages back. And the President felt that—that it was worth
taking some risk here.
<br/><br/><strong>Mr. LIMAN</strong>: Did the Secretary of State and the Secretary of Defense express objections?
<br/><br/><strong>Mr. POINDEXTER</strong>: They expressed, as opposed to some reports,
very strong, vociferous objection, and clearly laid out for the President the other side of the issue.
<br/><br/><strong>Mr. LIMAN</strong>: And without going into undue detail, could you just
tick off the points they made?
<br/><br/><strong>Mr. POINDEXTER</strong>: Well, they are the obvious points that have
been made since this all has become public. Secretary Shultz was
concerned about our operation to staunch the flow of arms into
Iran, which is one of the methods that we are using to try to stop
the war between Iran and Iraq.<br/><br/>
After all, our policy objective there is to stop the war. The policy
objective is not to have an arms embargo. You don't just go out and
have an arms embargo for nothing. The reason we have an arms
embargo against Iran is because we want to try to influence the
end of the war, but in its simplest terms, what was being proposed
here was not in accordance with that particular method that we
were using.<br/><br/>
He was concerned that if the European countries found out
about it, that it would lessen their willingness to cooperate. In reality, though, in my opinion, we have never had good cooperation
from anybody on Operation Staunch. The European countries continued to send military equipment and supplies into Iran. Iran has
been able to carry on a war for 6, going on 7 years now, I guess.
Other objections were that it was contrary to the Arms Export
Control Act. Secretary Weinberger had slightly different reasons,
but they are generally along the same lines.
<br/><br/><strong>Mr. LIMAN</strong>: And there is no doubt in your mind that the President listened to and understood those objections?
<br/><br/><strong>Mr. POINDEXTER</strong>: I have a very vivid recollection of that meeting,
and it was in the residence. The President pulled a footstool up to
the coffee table and sat there very quietly, as is his nature, listening to all of the discussion up to that point, listening to Secretary
Shultz, to Secretary Weinberger, Mr. McFarlane. I had very little
comment. And I don't recall the Chief of Staff saying very much.
Mr. McMahon was there and commented a little bit about some
of the technical aspects of the initiative. I had spoken to Director
Casey about the meeting before it took place, and knew at that
point that Director Casey was in favor of the idea.
And the President listened to all this very carefully, and at the
end of the discussion, at least the first round, he sat back and he
said something to the effect—and this is not a direct quote, but it
was something to the effect that "I don't feel we can leave any
stone unturned in trying to get the hostages back. We clearly have
a situation here where there are larger strategic interests, but it is
also an opportunity to get the hostages back, and I think that we
ought to at least take the next step."


