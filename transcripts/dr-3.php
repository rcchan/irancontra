<strong>Mr. SMILJANICH</strong>: Now, on November 24th, you were
told by the Attorney General about the diversion of funds; is that
correct?
<br/><br/><strong>Mr. REGAN</strong>: Yes.
<br/><br/><strong>Mr. SMILJANICH</strong>: That's Monday. He told you before the information got to the President?
<br/><br/><strong>Mr. REGAN</strong>: Yes. Not in detail but just as— there has been some
diversion of funds here, apparently, and this is information we got
to get to the President.
<br/><br/><strong>Mr. SMILJANICH</strong>: All right. What was your reaction when you
heard that information?
<br/><br/><strong>Mr. REGAN</strong>: Horror.
<br/><br/><strong>Mr. SMILJANICH</strong>: All right. Did you then meet with the President
and the Attorney General that morning and give the President this
information, or exactly what did you do?
<br/><br/><strong>Mr. REGAN</strong>: The President had a political leader coming in from,
I believe it was an African political leader coming in around 11:30,
so we shoe-horned Ed Meese in for just a few minutes to alert the
President that he had some bad news, but I think this is the phrase
he used. He said, "I have a few things to button up, then I want to
get back to you and tell you the full story." So we agreed that we
would meet after the NSPG meeting in the Oval Office.
<br/><br/><strong>Mr. SMILJANICH</strong>: Did he say anything in this initial conversation
about anything in connection with the diversion of funds?
<br/><br/><strong>Mr. REGAN</strong>: I think he may have said a possible diversion of
funds, but no names were specified nor amounts nor any details.
<br/><br/><strong>Mr. SMILJANICH</strong>: You were then present after the NSPG meeting
with the Attorney General and the President when the Attorney
General gave the President a little more details about what he had
discovered?
<br/><br/><strong>Mr. REGAN</strong>: That's right.
<br/><br/><strong>Mr. SMILJANICH</strong>: It was just the three of you in the Oval Office?
<br/><br/><strong>Mr. REGAN</strong>: That's it.
<br/><br/><strong>Mr. SMILJANICH</strong>: What was the President's reaction upon-
<br/><br/><strong>Mr. REGAN</strong>: Deep distress, deep distress.
You know, the question has been asked, I've seen it in the paper
time and time again: did the President know? Let me put it this
way. This guy I know was an actor, and he was nominated at one time for an Academy Award, but I would give him an Academy
Award if he knew anything about this when you watched his reaction to express complete surprise at this news on Monday afternoon the 24th.
He couldn't have known it.
<br/><br/><strong>Mr. SMILJANICH</strong>: It would be fair to say, based on his reaction, he
didn't think it was such a neat idea?
<br/><br/><strong>Mr. REGAN</strong>: No, I don't— I wouldn't characterize it that way at
all.
<br/><br/><strong>Mr. SMILJANICH</strong>: OK.
Now, you knew nothing about the fact that there had been a
mark-up of the cost of these missiles in connection with their shipment to Iran?
<br/><br/><strong>Mr. REGAN</strong>: That's what Meese told us.
<br/><br/><strong>Mr. SMILJANICH</strong>: Your understanding was this was a direct cost
transaction?
<br/><br/><strong>Mr. REGAN</strong>: Yes.
<br/><br/><strong>Mr. SMILJANICH</strong>: All right. You had a term for that, you have a 
term for your understanding of what kind of deal this was on Wall
Street?
<br/><br/><strong>Mr. REGAN</strong>: On Wall Street the deal, as I understand it, was
NPH.
<br/><br/><strong>Mr. SMILJANICH</strong>: What does that stand for?
<br/><br/><strong>Mr. REGAN</strong>: "No profit here." That is, you're selling it at cost, no
profit. That's what I thought we were doing, selling it out of our
stocks or replenishing Israeli stocks at our cost.
<br/><br/><strong>Mr. SMILJANICH</strong>: Was a 600-percent markup consistent with a
vision of a new bilateral relationship with Iran?
<br/><br/><strong>Mr. REGAN</strong>: I'll say one thing, that may be another way to try to
balance the budget.


