  Ethics and Public Officials</h2>

<h3>Beyond &quot;Obey or Resign&quot;</h3>
<div style="margin: 0px 0px 0px 20px">
  <p>A stark view of public service is that officials who oppose policies on moral grounds should “obey or resign.” Many ethicists find this formulation too stark. What should a public official do when he or she has strong moral objections to a policy? A threat to resign might be an effective strategy. Resignations over policy differences are rare; so are threats to resign, particularly public threats. There was a prominent exception in 1986 when the Reagan Administration announced a plan to require lie detector tests for all public officials with access to highly classified information, Secretary of State George Shultz told reporters: “The minute in this government I am told that I'm not trusted is the day I leave.” The White House quickly backed down from the plan and Shultz was credited with helping to change a misguided policy. There is a <a href="https://case.hks.harvard.edu/george-shultz-and-the-polygraph-test/" class="content_link">Harvard Kennedy School case study</a> on this issue alone, “George Shultz and the Polygraph Test.” </p>
  <p>Shultz also had strong objections to the arms-for-hostages idea. He was opposed to selling arms to Iran and objected to the idea in August, 1985. He <a href="/v-gs2.php" class="content_link">testified</a> to expressing forceful objections at another meeting in December. His objections continued but, as the Tower Commission later put it, he “distanced” himself “from the march of events.”&nbsp; Shultz was later criticized for threatening to resign when his personal honor was at stake but failing to so when, as Gutmann and Thompson put it, “national blackmail and violations of democratic principles occurred.” </p>
  <p>Shultz <a href="/v-gs3.php" class="content_link">testified</a> about difficulties during period of time when he was seen as disloyal to President Reagan on this issue: “I could see people were calling for me to resign if I can't be loyal to the President, even including some of my friends and people who had held high office and should know that maybe there's more involved than they're seeing.” His critics, including Rep. DeWine, <a href="/v-gs7.php" class="content_link">objected</a> that Shultz should have done more; instead, he “walked off the field when the score was against you.” </p>
</div>
