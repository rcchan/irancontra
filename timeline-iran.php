 <td><p>
    <b>Iran Evolves from Secular U.S. Ally to Islamic Republic and U.S. enemy</b>
</p>
<ul type="disc">
    <li>
        <b>1953-1979</b>
        : Mohammad Reza Shah Pahlavi (known as &#8220;the Shah) is secular and authoritarian ruler of Iran. Iran is one of the United States&#8217; strongest
        allies in the Middle East.
    </li>
    <li>
        <b>1978</b>
        : Riots and demonstrations break out across Iran, largely in response to the Shah&#8217;s secularism and close relationship with the U.S.
    </li>
    <li>
        <b>1979</b>
        : Riots and demonstrations grow increasingly numerous, frequent, and violent, ultimately culminating in the Iranian Revolution.
    </li>
    <li>
        <b>January 1979</b>
        : The Shah leaves Iran, and the country is declared an Islamic Republic by Ayatollah Ruhollah Khomeini. The Ayatollah severs all ties with the U.S. and
        declares Israeli illegitimate.
    </li>
    <li>
        <b>November 4, 1979:</b>
        Muslim Followers of the line of the Imam, a fundamentalist, anti-imperialist group made up predominately of young radical revolutionaries, seized the
        U.S. embassy in Tehran. End of cordial diplomacy between the two nations<b></b>
    </li>
    <li>
        <b>1980-90</b>
        : Iran-Iraq War.
    </li>
</ul>
<p>
    <b>Arms-for-Hostages</b>
</p>
<ul type="disc">
    <li>
        <b>1983</b>
        : U.S. actively engaged in arms embargo, Operation Staunch.
    </li>
    <li>
        <b>1983</b>
        : Adnan Khashoggi first meets with Robert McFarlane, Theodore Shackley meets Iranians Manucher Hashemi, Manucher Ghorbanifar and Hassan Karoubi.
    </li>
    <li>
        <b>January 1984</b>
        : McFarlane formally requests the NSC to formally examine ways to influence Iran. Report conveys an impasse.
    </li>
    <li>
        <b>March 1984</b>
        : Religious fundamentalist group Islamic Holy War kidnaps William F. Buckley, the CIA chief in Beirut, Lebanon. More American hostages taken in the
        following years.
    </li>
    <li>
        <b>1985</b>
        : Ghorbanifar and Khashoggi meet in Hamburg and devise skeleton of plan that will become Iran arms deal.
    </li>
    <li>
        <b>Summer 1985</b>
        : Israeli representatives drawn into discussion
    </li>
    <li>
        <b>July 1, 1985</b>
        : President Reagan publicly denounces bartering with terrorists.
    </li>
    <li>
        <b>July 3, 1985</b>
        : McFarlane meets with Israeli David Kimche, who is in the U.S. on behalf of the Israelis who had met with Khashoggi and Ghorbanifar. The
        arms-for-hostages deal is first outlined, as is the prospect of improving the U.S.-Iran relationship.
    </li>
    <li>
        <b>July 16, 1985</b>
        : McFarlane meets with Reagan and his Chief of Staff Regan while Reagan is in the hospital recovering from surgery. They discuss the possibility of
        selling arms to Iran via Israeli in order to get the release of the hostages and to open communications with Iran. The details of this visit are hazy,
        but McFarlane came away from it with the idea that the President had encouraged him to go forward with discussions with the Iranians and Israelis.
    </li>
    <li>
        <b>August 1985</b>
        : Reagan approves the plan to allow Israeli to sell U.S.-made weapons to Iran.
    </li>
    <li>
        <b>August 20, 1985</b>
        : first load of missiles sent from Israeli to Iran.
    </li>
    <li>
        <b>September 15, 1985</b>
        : American hostage Benjamin Weir released. Colonel Oliver North brought in to deal with logistics
    </li>
    <li>
        <b>November 1985</b>
        : second load of missiles sold. Major General Richard Secord brought in to help replenish Israeli&#8217;s supply of weapons.
    </li>
</ul>
<p>
    <b>The Enterprise and Diversion of funds to Nicaragua Contras</b>
</p>
<ul type="disc">
    <li>
        <b>November 1985</b>
        : first funds from arms sales diverted to Nicaraguan Contras through Secord&#8217;s Enterprise.
    </li>
    <li>
        <b>January 17, 1986</b>
        : President Reagan signs a Presidential Finding authorizing the transfer of arms to Iran through the Enterprise in order to release the U.S. from
        liability.
    </li>
    <li>
        <b>February 1986</b>
        : North, Ghorbanifar and Iranian representative meet in Germany, work out a plan to send further arms in exchange for the release of U.S. hostages.
    </li>
    <li>
        <b>February 27, 1986</b>
        : US sent 1,000 TOWs to Iran, no hostages released.
    </li>
    <li>
        <b>April 1986</b>
        : Oliver North writes the &#8220;Diversion Memorandum&#8221; which clearly lays out what is going on with the transfer of funds to Contras.
    </li>
</ul>
<p>
    <b>Secord&#8217;s Second Channel:</b>
</p>
<ul type="disc">
    <li>
        <b>August 1986</b>
        : Secord and his business partner Albert Hakim meet with a new Iranian contact, Ali Hashemi Bahramani, and open up a second channel in Iran.
    </li>
    <li>
        <b>October 1986</b>
        : North meets with this new group of Iranians, and Hakim, serving as a U.S. representative, works out a nine-point plan with the group.
    </li>
    <li>
        <b>October 28, 1986</b>
        : first shipment through second channel sent to Iran. Iran paid $3.6 million to the Enterprise, of which $2 million was turned over to the CIA, who had
        official supplied the weapons. The remainder was diverted to the Contras.
    </li>
    <li>
        <b>November 1986</b>
        : two Lebanese newspapers break story of arms deal, arms deal comes to an end.
    </li>
</ul>
