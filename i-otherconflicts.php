<div style="margin: 0px 0px 0px 20px">
  <p><strong><em>Iran-Iraq War</em></strong> (1980-90)<br /></p><p>
The Iran-Iraq war was the main reason why Iran was so need of weapons, and thus was willing to purchase them from the U.S. and Israel. The Reagan Administration's decision to sell arms to Iran was particularly shocking because the U.S. had publicly supported Iraq, along with Saudi Arabia, Kuwait, and other small mideast nations. Iran's public support was largely limited to Syria and Libya. Tensions between the two bordering countries had risen during the decades preceding the 1980s, largely stemming from territorial and ethnic conflicts. In the view of each country, the other had ultimately provoked the open fighting that broke out in September 1980. On September 4, Iran bombed a series of posts on the shared border. On September 22, Iraq's army surprised Iran by crossing the border into Iran, in an attempt to claim Khuzestan, the Iranian region on the other side of Shatt al-'Arab river, which had traditionally served as a marking point of the countries' shared border. Saddam Hussein, the then-President of Iraq, purposefully attacked Iran at a point when its relationship with the U.S. was strained. </p>
  <p>Though the Iraqi army gained much ground at first, by 1982 the Iranian army, working with the Revolutionary Guards, fought back so effectively that Hussein attempted to withdraw the Iraqi army and resume peaceful relations between the two countries. However, the Iranian administration in power, led by the Ruholla Khomeini, was so incensed by Iraq's actions that they attempted to overthrow President Hussein, and soon crossed the border into Iraq. Ultimately, the war became a stalemate: a series of back-and-forth infantry, air, missile, and sea attacks. </p>
  <p>A number of human rights violations occurred on both sides. For example, Iran took advantage of child soldiers, while Iraq used chemical warfare on its own people: tens of thousands of Kurdish citizens whom the government believed to be supportive of Iran. Through the eighties, Iraq attempted to sue Iran for peace, but it wasn't until 1988 that Iran accepted a cease-fire, and in 1990 final terms to end the war were agreed upon. </p>
  <p><strong><em>Iran and Israel</em></strong><br /></p><p>
    The extent of Iranian-Israeli cooperation that came about as a result of the Iran-Contra affair has been seen by some as even more surprising than the U.S.-Iran relationship. Prior to 1979's Iranian Revolution, Iran and Israel had been on civil terms, but this took a dramatic turn in the opposite direction following the Revolution. Iran's fundamentalist Islamic government advocated for the destruction of Israel and the reinstatement of the Islamic Palestinian state. The Iranian government almost immediately refused to engage in any trade or diplomatic relationship with Israel. </p>
