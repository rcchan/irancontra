<h3>The Role of Congressional Investigations</h3>
<div style="margin: 0px 0px 0px 20px">
  <p>What is the purpose of a congressional hearing of this nature? Before beginning his questioning of Oliver North, Mr. Nields <a href="transcripts/on-1.php" class="content_link">said</a> “a principal purpose of these hearings [is] to replace secrecy and deception with disclosure and truth.” In his <a href="transcripts/on-1.php" class="content_link">reply</a>, North famously said: “I came here to tell you the truth, the good, the bad, and the ugly.” <br />
    <br />
    It is clear that some of the key witnesses at the hearings thought that the proceedings were unfair. For example, North <a href="transcripts/on-5.php" class="content_link">testified</a> at one point about issues that have to be debated “not just by pulling people before this group and hammering at them and haranguing them and reducing it to pettiness.” One of the issues that North’s supporters would consider petty and unfair were questions concerning expenditures and possibly personal financial gain, particularly from the security system that was installed at his home. (<a href="transcripts/on-8.php" class="content_link">Here</a> is North’s famous defense of the security system where he offers to meet Abu Nidal “on equal terms anywhere in the world.”) <br />
    North’s lawyer, Brendan Sullivan, famously <a href="transcripts/on-15.php" class="content_link">interjected</a> at one point, when yet another one of his objections was overruled: “I am not a potted plant.” He was objecting more broadly to the loose rules of the evidence and the hypothetical nature of some questions; Sullivan called them “dreamland scenarios.” <br />
    <br />
    <strong>ADD: VIEWERSHIP DATA AND PUBLIC OPINION PIECE.</strong><br />
    <br />
    Finally, the “limited use immunity” that was provided to those who testified before Congress ended up having a much broader effect than intended. Even though the Independent Counsel went to great lengths to avoid having Congressional testimony taint the development of the prosecution’s case, the convictions they won were <a href="prosecutions.php" class="content_link">overturned </a>on appeal because the court was not convinced that the <em>jurors</em> had not been tainted by the Congressional hearings. </p>
