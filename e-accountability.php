<h3>Accountability and Covert Operations</h3>
<div style="margin: 0px 0px 0px 20px">
  <p>The Iran-Contra Affairs raise various issues about accountability and covert operations. But these issues went far beyond covert operations. They were about covert operations run out of the National Security Council. They were also about what some called the privatization of foreign policy, whereby third parties and third countries took actions to advance goals that the President could not pursue through regular channels. <br />
    <br />
    Senator Hatch <a href="transcripts/on-18.php" class="content_link">made it clear</a> that he did not think that the NSC should ever operate covert operations and that he opposed the diversion of funds from arms sales in Iran to help the Contras. In the same excerpt, he says that the hearings have demonstrated “the difficulties in “privatization of our foreign policy.” Similarly, Elliot Abrams <a href="transcripts/ea-4.php" class="content_link">testified </a> that having operations run by the NSC that were unknown to the Department of State was “a formula for disaster.” </p>
</div>
