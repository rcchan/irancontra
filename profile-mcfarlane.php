<h2>Robert McFarlane &nbsp;&nbsp;<font class="position">National Security Advisor</font></h2>

<p>Robert McFarlane served as President Reagan’s national security adviser from October 1983 until December 1985. His involvement in the Affairs began after Congress passed the Boland Amendment banning aid to the Contras, which led Reagan to ask him to keep the rebels alive “body and soul.” McFarlane assigned NSC staff member Oliver North to that job, although he later claimed to have told North not to raise money for the Contras in violation of that law.</p>
<p>In 1985, the press published reports of North's fundraising and Contra-support network, leading Congress to question McFarlane. He admitted responsibility for North’s actions as his superior but claimed a lack of knowledge of these activities. Evidence later showed this was not the case.</p>
<p>Indeed, NSC General Counsel Paul Thompson had highlighted six memos that North wrote to McFarlane about his actions that most obviously contradicted McFarlane’s testimony. McFarlane suggested that North alter these memos, which he told Independent Counsel was only to make them more accurate. But the edits North made clearly hid his Contra-aid efforts.</p>
<p>In March of 1988, McFarlane pleaded guilty to four misdemeanors of withholding information from Congress:</p>
<ol start="1" type="1">
  <li>He      pleaded guilty to lying to Lee Hamilton, chair of the House Permanent      Select Committee on Intelligence, when he said, “I can state with deep      personal conviction that at no time did I or any member of the National      Security Council staff violate the letter or spirit of the law […]. We did      not solicit funds or other support for military or paramilitary      activities.”</li>
  <li>He      also told Michael Barnes, chair of the House Foreign Affairs Subcommittee      on Western Hemisphere Affairs, “My actions, and those of my staff, have      been in compliance with both the spirit and the letter of the law […].      None of us has solicited funds, facilitated contacts for prospective      potential donors, or otherwise organized or coordinated the military or      paramilitary efforts.”</li>
  <li>In      addition, he falsely claimed to Hamilton, “North did not use his influence      to facilitate the movement of supplies to the resistance.”</li>
  <li>Finally,      McFarlane withheld information from the House Committee on Foreign      Affairs. When asked about third-country involvement in financing the      Contras, he replied, “I have seen the reports and I have heard that the      [nationals of such third country] have contributed. The concrete character      of that is beyond my ken.”</li>
</ol>
<p>Walsh accepted his plea first and foremost because McFarlane agreed to cooperate with other investigations. Moreover, in 1987, McFarlane attempted to commit suicide, a sign that he regretted his involvement with the Iran-Contra Affairs. McFarlane also helped the investigators at an early date, even before he pleaded guilty.</p>
<p>McFarlane was sentenced to two years probation and 200 hours of community service, and was ordered to pay a $20,000 fine. However, in 1992, President George H.W. Bush pardoned him.<br />
  <br />
</p>
