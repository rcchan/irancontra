mso-fareast-theme-font:minor-latin;mso-ansi-language:EN-US;mso-fareast-language:
EN-US;mso-bidi-language:AR-SA'>[26]</span></span><![endif]></span></span></a>
Reagan's blunders during the November 19<sup>th</sup> conference set into motion
public discourse on the President's credibility and role in the whole affair. <o:p></o:p></span></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;text-indent:
.25in;line-height:200%'><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif"'>On November 21<sup>st</sup>, Oliver
North engaged in what he would later be referred to as a shredding party,
destroying potentially incriminating documents, helped by his secretary </span><a
href="http://www.brown.edu/Research/Understanding_the_Iran_Contra_Affair/profile-hall.php"><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'>Fawn
Hall</span></a><span style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'>,
in anticipation of the Justice Department lawyers coming to search his office
the next day. North's shredding, however, was not enough. He did not destroy
the smoking gun of the connection between the Iran arms sales and the funding
for the Nicaraguan contras, the Diversion Memo. After </span><a
href="http://www.brown.edu/Research/Understanding_the_Iran_Contra_Affair/profile-meese.php"><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'>Attorney
General Meese</span></a><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif"'>, Assistant Attorney General Reynolds,
and Chief of Staff to Attorney General Richardson interviewed North about the
document, it was a race against the media for the Reagan administration to
release this information to the public. Fearing accusations of a Watergate
style cover-up and more seriously the possibility of impeachment, President
Reagan himself publicly acknowledged diversion scheme of the arms deal to the
public.<a style='mso-footnote-id:ftn27' href="#_ftn27" name="_ftnref27" title=""><span
class=MsoFootnoteReference><span style='mso-special-character:footnote'><![if !supportFootnotes]><span
