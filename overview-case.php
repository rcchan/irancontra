style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'>Lieutenant
Colonel Oliver North</span></a><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif"'>, deputy director of political-military
affairs for the National Security Council staff was deeply involved in both the
Iran and Contra affairs. <b style='mso-bidi-font-weight:normal'><o:p></o:p></b></span></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;text-indent:
.5in;line-height:200%'><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif"'>Like the NSC, the CIA evolved with the
different Presidential administrations. Under Eisenhower, the 1955 NSC
directive outlined the spectrum of the CIA's covert operations in an effort to
turn the CIA into a virtual Cold War machine against Communism- to create
and exploit troublesome problems for international Communism reduce
international Communist control over any areas of the world and develop
underground resistance and facilitate covert and guerilla operations.<a
style='mso-footnote-id:ftn5' href="#_ftn5" name="_ftnref5" title=""><span
class=MsoFootnoteReference><span style='mso-special-character:footnote'><![if !supportFootnotes]><span
