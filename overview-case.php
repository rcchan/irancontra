EN-US;mso-bidi-language:AR-SA'>[24]</span></span><![endif]></span></span></a>
implying he dealt with terrorist organizations. Further contradictions were
made during the press conference on the 19<sup>th</sup> when Reagan stated
that, we did not condone and do not condone the shipment of arms from other
countries.<span class=MsoFootnoteReference> <a style='mso-footnote-id:ftn25'
href="#_ftn25" name="_ftnref25" title=""><span style='mso-special-character:
