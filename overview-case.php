mso-ansi-language:EN-US;mso-fareast-language:EN-US;mso-bidi-language:AR-SA'>[16]</span></span><![endif]></span></span></a>
The deal was wholly managed through private actors- Ghorbanifar for Iran and
Schwimmer and Nimrodi for Israel. Lt. Colonel Oliver North was brought into the
Iran affair by McFarlane to manage logistics in the interest of the United
States. North continued to stay involved in Iran when Poindexter succeeded
McFarlane. On <span style='mso-bidi-font-weight:bold'>September 15, 1985, </span>American
hostage Benjamin Weir was released after 408 more TOWs were shipped to Iran.
Any profits from the deal went to Ghorbanifar, Schwimmer or Nimrodi. </p>

<p style='margin:0in;margin-bottom:.0001pt;text-indent:.5in;line-height:200%'>Major
General Richard Secord was brought into the Iran affair by North to help
resupply Israel's weapon store and organize logistical issues such as moving
sensitive material between Israel and Iran. In November 1985, a second load
of missiles was sold to Iran. <span style='mso-spacerun:yes'></span>The second
sale provided the first funds that were diverted to the Nicaraguan Contras. To
complete the diversion covertly, and without the knowledge of Congress, Secord
and Hakim established a company called the Stanford Technology Trading Group
International which was commonly known as The Enterprise. Israel transferred
$1 million to an Enterprise-owned, Secord-Hakim Lake Resources Swiss bank
account for the second arms shipment. This account had previously been used only
for Nicaraguan Contra business.<a style='mso-footnote-id:ftn17' href="#_ftn17"
name="_ftnref17" title=""><span class=MsoFootnoteReference><span
style='mso-special-character:footnote'><![if !supportFootnotes]><span
