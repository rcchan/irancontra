To help with logistics, North brought Secord on board rather than using someone
from the U.S. government because Secord had close ties with leading Portuguese
arms dealer, Defex. </p>

<p style='margin:0in;margin-bottom:.0001pt;text-indent:.5in;line-height:200%'>The
first effort to get airport clearance was described to the Portuguese Foreign
Ministry as Defex working with a retired American general to ship arms to Iran.
This confused Portuguese officials because of what they understood as the
United States' opposition to all shipments of arms to Iran under Operation
Staunch. Portugal was now skeptical of the whole affair; especially in regards
to who was making this arms shipment request- the United States Government, or
a private citizen. When that request was denied, North worked with Dewey R.
Clarridge, Chief of the European division of the CIA, to help deal with the Lisbon
airport crisis and try again to obtain airport clearance. Portugal firmly
insisted on receiving a formal acknowledgement they were being asked to help
in a weapons shipment as not to later be charged with violating Operation
Staunch.<a style='mso-footnote-id:ftn22' href="#_ftn22" name="_ftnref22"
title=""><span class=MsoFootnoteReference><span style='mso-special-character:
footnote'><![if !supportFootnotes]><span class=MsoFootnoteReference><span
