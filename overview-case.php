EN-US;mso-bidi-language:AR-SA'>[27]</span></span><![endif]></span></span></a> <o:p></o:p></span></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;text-indent:
.5in;line-height:200%'><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif"'>November 25, 1986 Reagan held a press
conference where Attorney General Meese responded to the majority of questions.
Meese said that the affair did not going any higher than Admiral Poindexter.<a
