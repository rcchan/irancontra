Of the $1 million, only $150,000 was spent on the weapons, the other $850,000
was diverted, by North, to be used to support the Contras. Ordinarily the
entire million would've been paid back to the Israelis, but in this instance,
North told them we used it for the purpose of the Contras, and they
acknowledged that and they never asked for the money back.<a
style='mso-footnote-id:ftn18' href="#_ftn18" name="_ftnref18" title=""><span
class=MsoFootnoteReference><span style='mso-special-character:footnote'><![if !supportFootnotes]><span
