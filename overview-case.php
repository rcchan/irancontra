In January of 1986, the diversion scheme continued when Ghorbanifar suggested
that any extra money made through the arms sales be diverted to aiding the
Contras. McFarlane's successor, John Poindexter, approved this plan. </p>

<p style='margin:0in;margin-bottom:.0001pt;line-height:200%'><i
style='mso-bidi-font-style:normal'><u>Lisbon Airport Crisis<o:p></o:p></u></i></p>

<p style='margin:0in;margin-bottom:.0001pt;line-height:200%'><span
style='mso-tab-count:1'> </span>The Second shipment of
arms-for-hostages mentioned briefly above was a logistical nightmare that North
described as a horror story.<a style='mso-footnote-id:ftn19' href="#_ftn19"
name="_ftnref19" title=""><span class=MsoFootnoteReference><span
style='mso-special-character:footnote'><![if !supportFootnotes]><span
class=MsoFootnoteReference><span style='font-size:12.0pt;line-height:115%;
font-family:"Times New Roman","serif";mso-fareast-font-family:"Times New Roman";
mso-ansi-language:EN-US;mso-fareast-language:EN-US;mso-bidi-language:AR-SA'>[19]</span></span><![endif]></span></span></a>
The original plan was that on November 22<sup>nd</sup>, 1985, 120 Hawk missiles
would be shipped from Israel to Portugal on an Israeli 747. In Portugal the
weapons would be unloaded, stored, and reloaded on a non-Israeli plane and
shipped to Iran in two intervals. First, eighty Hawks would be sent, followed
by the release of hostages. Second, contingent upon the hostages' release, the remaining
forty would then be sent to Iran.<a style='mso-footnote-id:ftn20' href="#_ftn20"
name="_ftnref20" title=""><span class=MsoFootnoteReference><span
style='mso-special-character:footnote'><![if !supportFootnotes]><span
