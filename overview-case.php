EN-US;mso-bidi-language:AR-SA'>[5]</span></span><![endif]></span></span></a>
Eisenhower did qualify that the covert operations had to be consistent with
U.S. foreign and military policies. The War Powers Resolution, which was
created as a check on presidential power by Congress did not include a check of
covert wars and paramilitary activities that the CIA was authorized to conduct.
The CIA director during the Reagan administration was </span><a
href="http://www.brown.edu/Research/Understanding_the_Iran_Contra_Affair/profile-casey.php"><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'>William
Casey</span></a><span style='font-size:12.0pt;line-height:200%;font-family:
"Times New Roman","serif"'>.<o:p></o:p></span></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;line-height:
200%'><b style='mso-bidi-font-weight:normal'><u><span style='font-size:12.0pt;
line-height:200%;font-family:"Times New Roman","serif"'>The Nicaraguan Story<o:p></o:p></span></u></b></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;line-height:
200%'><i style='mso-bidi-font-style:normal'><u><span style='font-size:12.0pt;
line-height:200%;font-family:"Times New Roman","serif"'>Somoza Dynasty<o:p></o:p></span></u></i></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;text-indent:
.25in;line-height:200%'><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif"'>The U.S. has long intervened in
Nicaraguan affairs, aiming to keep its political developments amicable with and
aligned to American interests. As early as 1912 the U.S. has utilized military
force to quell rebellions against American approved leaders or to help overthrow
unwanted regimes. Therefore, when U.S. trained head of the Nicaraguan National
Guard, Somoza Garcia, forcefully took power in 1936, the U.S. made no move to
protect the current administration under Augusto Cesar Sandino. Sandino's
murder marked the beginning of the Somoza dynastic rule which lasted for the
next 43 years. In 1961, the Sandinista National Liberation Front (FSLN),
named in honor of Sandino, was created in opposition to the Somoza dynasty.
Ideologically, the Sandinistas saw themselves as a Marxist-Leninist
organization with aims of turning Nicaragua into a socialist state. Inspired by
and closely connected to Cuba, the Sandinistas worked to create and consolidate
their power in the context of a cold war era where socialist revolutions and uprisings
were gaining in worldwide popularity.<o:p></o:p></span></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;line-height:
200%'><span style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'><span
style='mso-tab-count:1'> </span>In 1967, Anastasio Somoza Debayle,
son of Somoza Garcia, became president.&nbsp;He became notorious in Nicaragua
for suppressing opposition and focusing on self-enrichment while in power. For
example, in 1972, when an earthquake struck Managua, the capital of Nicaragua,
Somoza exercised emergency powers to address the earthquake which in
actuality resulted in him and his close friends confiscating the majority of
international aid sent to help rebuild Nicaragua. This event consolidated the
Nicaraguan's disapproval of Anastasio Somoza Debayle, especially among the
Sandinistas.<span style='mso-spacerun:yes'> </span><o:p></o:p></span></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;line-height:
200%'><span style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'><span
style='mso-tab-count:1'> </span>In 1974, the Sandinistas kidnapped
several Nicaraguan elites at a Christmas Party. Somoza responded to the affair by
declaring a state of siege which spiraled into a series of serious human rights
violations and guerilla attacks on peasants. In response, the United States,
hyper-sensitive to the threat of communism and in conjunction with a
contemporaneous trend of protecting human rights victims, began to pay
attention to Nicaraguan affairs for the first time since the Somoza dynasty commenced
in 1936. President Jimmy Carter's foreign policy was shaped not only by a consciousness
of human rights, but also by a fatigue of foreign intervention due to the
Vietnam War. President Carter cut off all aid to the Nicaraguan government
until it improved its human rights violations. Somoza responded by lifting the
state of siege. This was met by the Sandinistas re-initiating and expanding
their attacks which were now supported by business elites including Alfonso
Robelo, and academics, including Adolfo Calero.<o:p></o:p></span></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;line-height:
200%'><i style='mso-bidi-font-style:normal'><u><span style='font-size:12.0pt;
line-height:200%;font-family:"Times New Roman","serif"'>Sandinistas in Power:
U.S.-Nicaraguan relations still diplomatic<o:p></o:p></span></u></i></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;line-height:
200%'><span style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'><span
style='mso-tab-count:1'> </span>On July 19, 1979, the Sandinista uprising
culminated in their gaining full power in Nicaragua. The Sandinistas first move
as new political leaders was to declare a state of emergency and expropriate
land and businesses owned by the old dynastic family and friends, nationalize
banks, mines, and transit systems, abolish old courts, denounce churches, and
nullify the constitution, laws, and elections. A socialist state was born in
Nicaragua. President </span><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif";mso-fareast-font-family:"Times New Roman"'>Carter
immediately sent $99 million in aid to the FSLN in an attempt to keep the new
regime pro-U.S.. Simultaneously, however, Cuban officials were advising the FSLN
on foreign and domestic policy and the FSLN sought an alliance with the Soviet
bloc which they reached by March 1980 signing economic, cultural,
technological, and scientific agreements with the USSR.<span
style='mso-spacerun:yes'> </span>Deliveries of Soviet weapons from Cuba began
almost immediately after the signing of these agreements. <o:p></o:p></span></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;text-indent:
.5in;line-height:200%'><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif";mso-fareast-font-family:"Times New Roman"'>It
was mid-1980 when Jose Cardenal and Enrique Bermudez founded what would become
the Nicaraguan Democratic Force, or FDN, the main contra group (the Contras).
The Contras found support among the populations disaffected by Sandinista
policies i.e. protestant evangelicals, farmers, Nicaraguan Indians, Creoles, and
other disgruntled and disenfranchised parties. The Argentinean government was
the first to support the Contras. They directly oversaw the Contras, trained
the military forces, and chose the Contra leadership whereas the U.S. took on
the role of supplying money and arms. Many worried that the Contras were a
continuation of the Somoza regime because of their use of brutal tactics
against noncombatants and their alleged human rights abuses. <o:p></o:p></span></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;text-indent:
.5in;line-height:200%'><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif"'>Once it became clear to Washington that
the FSLN would not moderate its policies, President Carter authorized the CIA
to support resistance forces in Nicaragua including propaganda efforts, but not
including armed action. The Sandinistas supported expanding socialism abroad,
including sending weapons to leftist rebels in El Salvador beginning in 1980
and continuing for the next ten years. Some argue that this international
support from Nicaragua was also in effort to insure that the Soviets would
fully support and protect Nicaragua in case of a U.S. attack or
intervention.<span style='mso-spacerun:yes'> </span>Sandinista support for the
Salvadoran rebels had a profound impact on U.S.-Nicaragua relations throughout
the 80's.<o:p></o:p></span></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;line-height:
200%'><i style='mso-bidi-font-style:normal'><u><span style='font-size:12.0pt;
line-height:200%;font-family:"Times New Roman","serif"'>Reagan Administration:
Intervention and Propoganda<o:p></o:p></span></u></i></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;text-indent:
.5in;line-height:200%'><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif"'>January 20, 1981, </span><a
href="http://www.brown.edu/Research/Understanding_the_Iran_Contra_Affair/profile-reagan.php"><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'>Ronald
Reagan</span></a><span style='font-size:12.0pt;line-height:200%;font-family:
"Times New Roman","serif"'> was inaugurated during a rightward shift in U.S.
politics. He quickly cut off all aid to FSLN indefinitely due to the
Sandinista's continued support of Salvadoran rebels. In response, the
Sandinistas consolidated power and expanded arrests of perceived dissidents
under the belief that the U.S. would invade. On December 1, 1981, Reagan signed
an order that allowed the CIA to support the Contras with arms, equipment, and
money. This order was implemented in conjunction with an overall strengthening
of U.S. presence in Central America and the belief that covert activities are
the most effective way to put pressure on a regime. <span
style='mso-spacerun:yes'></span>This shift of foreign policy away from the
Carter administration's non-intervention culminated in June 1982 with the
Reagan Doctrine which called for supporting democratization everywhere. It was
at this point that the goal of the covert operations in Nicaragua shifted away
from one of simply interdicting arms to one of supporting a change in
government. Iran-Contra historian Theodore Draper, among others, argued, that
this was the real goal all the long. <o:p></o:p></span></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;line-height:
200%'><span style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'><span
style='mso-tab-count:1'> </span>To help popularize the foreign
policy changes of the Reagan administration certain propaganda and media
initiatives were implemented to sway public and congressional opinion. In
January of 1983, </span><a
href="http://www.fas.org/irp/offdocs/nsdd/23-1966t.gif"><span style='font-size:
12.0pt;line-height:200%;font-family:"Times New Roman","serif"'>National
Security Decisions Directive</span></a><span style='font-size:12.0pt;
line-height:200%;font-family:"Times New Roman","serif"'> was signed, entitled
Management of Public Diplomacy Relative to National Security, institutionalizing
public diplomacy. In effect, it was a special planning group within the NSC to
coordinate public diplomacy campaigns.<a style='mso-footnote-id:ftn6'
href="#_ftn6" name="_ftnref6" title=""><span class=MsoFootnoteReference><span
style='mso-special-character:footnote'><![if !supportFootnotes]><span
class=MsoFootnoteReference><span style='font-size:12.0pt;line-height:115%;
font-family:"Times New Roman","serif";mso-fareast-font-family:Calibri;
mso-fareast-theme-font:minor-latin;mso-ansi-language:EN-US;mso-fareast-language:
EN-US;mso-bidi-language:AR-SA'>[6]</span></span><![endif]></span></span></a>
This group was America's first peacetime propaganda ministry. Every
administration tries to influence public opinion, but not until Reagan was it so
institutionalized. Another use of white propaganda,<span style='mso-bidi-font-weight:
bold'> which </span></span><a
href="http://www.brown.edu/Research/Understanding_the_Iran_Contra_Affair/profile-channellmiller.php"><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif";
mso-bidi-font-weight:bold'>Richard Miller</span></a><span style='font-size:
12.0pt;line-height:200%;font-family:"Times New Roman","serif";mso-bidi-font-weight:
bold'> described as &quot;actually putting out [the] truth, straight
information, not deception,&quot;</span><span style='font-size:12.0pt;
line-height:200%;font-family:"Times New Roman","serif"'> was the State
Department's Group of Latin American Public Diplomacy (S/LPD).<a
style='mso-footnote-id:ftn7' href="#_ftn7" name="_ftnref7" title=""><span
class=MsoFootnoteReference><span style='mso-special-character:footnote'><![if !supportFootnotes]><span
class=MsoFootnoteReference><span style='font-size:12.0pt;line-height:115%;
font-family:"Times New Roman","serif";mso-fareast-font-family:Calibri;
mso-fareast-theme-font:minor-latin;mso-ansi-language:EN-US;mso-fareast-language:
EN-US;mso-bidi-language:AR-SA'>[7]</span></span><![endif]></span></span></a>
This group, in actuality, reported directly to the NSC despite being housed
within the State Department. Both committees utilized a variety of media
propaganda and control efforts. A fourteen page memorandum dated March 20, 1985
from North to National Security Advisor </span><a
href="http://www.brown.edu/Research/Understanding_the_Iran_Contra_Affair/profile-mcfarlane.php"><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'>Robert
McFarlane</span></a><span style='font-size:12.0pt;line-height:200%;font-family:
"Times New Roman","serif"'> explained over 80 publicity stunts to influence
public and congressional opinion before upcoming Contra aid votes.<a
style='mso-footnote-id:ftn8' href="#_ftn8" name="_ftnref8" title=""><span
class=MsoFootnoteReference><span style='mso-special-character:footnote'><![if !supportFootnotes]><span
class=MsoFootnoteReference><span style='font-size:12.0pt;line-height:115%;
font-family:"Times New Roman","serif";mso-fareast-font-family:Calibri;
mso-fareast-theme-font:minor-latin;mso-ansi-language:EN-US;mso-fareast-language:
EN-US;mso-bidi-language:AR-SA'>[8]</span></span><![endif]></span></span></a>
The public diplomacy officials also leaked select pieces of information that
they wanted made public to journalists who favored Reagan. Strategic leaking
and declassification of documents allowed the Executive Branch to manage the
public perceptions of the American efforts in South America. <o:p></o:p></span></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;line-height:
200%'><i style='mso-bidi-font-style:normal'><u><span style='font-size:12.0pt;
line-height:200%;font-family:"Times New Roman","serif"'>Boland I &amp; II and
the Beginnings of Covert Defiance of Congress<o:p></o:p></span></u></i></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;line-height:
200%'><span style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'><span
style='mso-tab-count:1'> </span>Despite all the propaganda efforts, a
series of high-profile articles began to divide the Executive Branch and the Legislature
over the topic of Nicaragua and Contra support. In 1982, the CIA gained a more
prominent role in the training and funding of the Contras. This attracted the attention
of <i style='mso-bidi-font-style:normal'>Newsweek,</i> whose cover story on
November 8, 1982 was entitled America's Secret War: Nicaragua. The story
outlined America's efforts to undermine the Sandinista government,<span
class=MsoFootnoteReference> <a style='mso-footnote-id:ftn9' href="#_ftn9"
name="_ftnref9" title=""><span style='mso-special-character:footnote'><![if !supportFootnotes]><span
class=MsoFootnoteReference><span style='font-size:12.0pt;line-height:115%;
font-family:"Times New Roman","serif";mso-fareast-font-family:Calibri;
mso-fareast-theme-font:minor-latin;mso-ansi-language:EN-US;mso-fareast-language:
EN-US;mso-bidi-language:AR-SA'>[9]</span></span><![endif]></span></a></span> and
prompted a heated editorial response in <i style='mso-bidi-font-style:normal'>The
Boston Globe</i>. This response article sparked Massachusetts Representative
Edward P. Boland to lead a congressional effort to end all funding of Nicaraguan
efforts. The first Congressional legislation aimed at preventing funding came
on December 21, 1982 with the </span><a
href="http://www.brown.edu/Research/Understanding_the_Iran_Contra_Affair/documents/d-nic-7.pdf"><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'>first
Boland Amendment</span></a><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif"'> which barred the use of funds for the
purpose of' overthrowing the government of Nicaragua or provoking a war between
Nicaragua and Honduras. <o:p></o:p></span></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;text-indent:
.5in;line-height:200%'><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif"'>In a joint session of Congress,
President Reagan said, The Congress shares both the power and the
responsibility for our foreign policy, but by the time Congress exercised said
shared power by passing Boland I, the Reagan administration had already
committed itself to supporting the Contras unconditionally and at any cost-
even if that meant defying Congress.<a style='mso-footnote-id:ftn10'
href="#_ftn10" name="_ftnref10" title=""><span class=MsoFootnoteReference><span
style='mso-special-character:footnote'><![if !supportFootnotes]><span
class=MsoFootnoteReference><span style='font-size:12.0pt;line-height:115%;
font-family:"Times New Roman","serif";mso-fareast-font-family:Calibri;
mso-fareast-theme-font:minor-latin;mso-ansi-language:EN-US;mso-fareast-language:
EN-US;mso-bidi-language:AR-SA'>[10]</span></span><![endif]></span></span></a> Open
defiance was impossible, so covert defiance was adopted as the Executive Branch's
new normal. Boland I left a loophole that the Reagan administration quickly
utilized- as long as the U.S. itself did not intend to overthrow the Nicaraguan
government, the U.S. could support the Contras under a different guise such as
humanitarian aid or by the solicitation of money from third-party funds and
private actors. Thus, Boland I had no real impact on the conduct of the war in
Nicaragua. <o:p></o:p></span></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;text-indent:
.5in;line-height:200%'><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif"'>During the second half of 1983, the CIA
helped the Contras to conduct air strikes on Sandino airport near Managua in
addition to other targets.&nbsp; The CIA used its own assets to implement some
of the covert actions in Nicaragua, including destroying several fuel
tanks.&nbsp; The CIA also placed mines in Nicaraguan harbors on January 7, 1984
and February 29, 1984, damaging several ships.&nbsp; The Contras initially took
credit for the mining, but it was later revealed by <i style='mso-bidi-font-style:
normal'>The Wall Street Journal </i>that the mines were placed by the CIA.
Furthermore, <i style='mso-bidi-font-style:normal'>The Wall Street Journal </i>disclosed
that Lieutenant Colonel </span><a
href="http://www.brown.edu/Research/Understanding_the_Iran_Contra_Affair/profile-north.php"><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'>Oliver
North</span></a><span style='font-size:12.0pt;line-height:200%;font-family:
"Times New Roman","serif"'>, a U.S. Marine who worked on the National Security
Council staff at the Reagan White House, had knowledge of and encouraged such
actions. Oliver North would become an integral figure in the Iran-Contra
Affair. <o:p></o:p></span></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;text-indent:
.5in;line-height:200%'><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif"'>Also at this time, National Security
advisor Robert McFarlane began meeting with Israeli counterpart David Kimche
inquiring to whether or not Israel would help support the Contras. The
solicitation of Israel proved unsuccessful, but a few months later, McFarlane
secured money from Saudi Arabia in support of the Contras. McFarlane would
later argue that he had not solicited the funds, but simply mentioning the loss
of the Contra aid was enough to insight the Saudis to provide money for the
cause. McFarlane was able to secure over $32 million from Saudi Arabia from 1984-1986.
North added on another $2 million from Taiwan throughout the affair. Later in
1984, some people within Reagan's administration began toying with the idea of
setting up a private tax-exempt organization to raise money for the Contras. </span><a
href="http://www.brown.edu/Research/Understanding_the_Iran_Contra_Affair/profile-channellmiller.php"><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'>Carl
Spitz Channell</span></a><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif"'> led this effort to secure private
funds, many of the larger donors meeting with Oliver North and even President
Reagan directly. </span><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif";mso-fareast-font-family:"Times New Roman"'><o:p></o:p></span></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;text-indent:
.5in;line-height:200%'><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif";mso-fareast-font-family:"Times New Roman"'>Realizing
the ineffectiveness of Boland I, Congress, still determined to stop the flow of
funds to Nicaragua, passed a </span><a
href="http://www.brown.edu/Research/Understanding_the_Iran_Contra_Affair/documents/d-nic-21.pdf"><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif";
mso-fareast-font-family:"Times New Roman"'>second Boland Amendment</span></a><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif";
mso-fareast-font-family:"Times New Roman"'> on October 12, 1984 which reads: <span
style='mso-spacerun:yes'></span><o:p></o:p></span></p>

<p class=MsoNormal style='margin-top:0in;margin-right:0in;margin-bottom:0in;
margin-left:.5in;margin-bottom:.0001pt;line-height:normal'><span
style='font-size:12.0pt;font-family:"Times New Roman","serif";mso-fareast-font-family:
"Times New Roman"'>During fiscal year 1985, no funds available to the Central
Intelligence Agency, the Department of Defense, or any other agency or entity
of the United States involved in intelligence activities may be obligated or
expended for the purpose or which would have the effect of supporting directly
or indirectly, military or paramilitary operations in Nicaragua by any nation,
group, organization, movement or individual. <o:p></o:p></span></p>

<p class=MsoNormal style='margin-top:0in;margin-right:0in;margin-bottom:0in;
margin-left:.5in;margin-bottom:.0001pt;line-height:normal'><span
style='font-size:12.0pt;font-family:"Times New Roman","serif";mso-fareast-font-family:
"Times New Roman"'><o:p>&nbsp;</o:p></span></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;line-height:
200%'><span style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif";
mso-fareast-font-family:"Times New Roman"'>Boland II left two loopholes for
getting money to the Contras. The first loophole, like that of Boland I, was to
solicit third-party funds from private donors or third party countries to give
money to the Contras. The second loophole was to use the NSC which is </span><a
href="http://www.brown.edu/Research/Understanding_the_Iran_Contra_Affair/n-contrasus.php"><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif";
mso-fareast-font-family:"Times New Roman"'>the </span><span style='font-size:
12.0pt;line-height:200%;font-family:"Times New Roman","serif"'>President's
principal forum for considering national security and foreign policy matters
with his senior national security advisors and cabinet officials</span></a><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'>&nbsp;based</span><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif";
mso-fareast-font-family:"Times New Roman"'> on the logic that the NSC is not
covered under Boland.&nbsp; Oliver North, on loan to the NSC from the Marine
Corps, began to undertake this activity. President Reagan trusted that North,
in conjunction with McFarlane, would make sure to keep the Contras together </span><a
href="http://www.brown.edu/Research/Understanding_the_Iran_Contra_Affair/v-on16.php"><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif";
mso-fareast-font-family:"Times New Roman"'>body and soul</span></a><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif";
mso-fareast-font-family:"Times New Roman"'>.<a style='mso-footnote-id:ftn11'
href="#_ftn11" name="_ftnref11" title=""><span class=MsoFootnoteReference><span
style='mso-special-character:footnote'><![if !supportFootnotes]><span
class=MsoFootnoteReference><span style='font-size:12.0pt;line-height:115%;
font-family:"Times New Roman","serif";mso-fareast-font-family:"Times New Roman";
mso-ansi-language:EN-US;mso-fareast-language:EN-US;mso-bidi-language:AR-SA'>[11]</span></span><![endif]></span></span></a>
The passage of Boland II led to creative means of operational support of the
contras: arms deals, air supply ops and intelligence support, and further solicitation
of additional third party funds.<o:p></o:p></span></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;text-indent:
.5in;line-height:200%'><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif";mso-fareast-font-family:"Times New Roman";
mso-bidi-font-style:italic'>Arms Deals</span><span style='font-size:12.0pt;
line-height:200%;font-family:"Times New Roman","serif";mso-fareast-font-family:
"Times New Roman"'>: In addition to seeking alternative funding, North and
others sought to provide the contras with arms and supplies. Oliver North
worked with </span><a
href="http://www.brown.edu/Research/Understanding_the_Iran_Contra_Affair/profile-secord.php"><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif";
mso-fareast-font-family:"Times New Roman"'>Richard Secord</span></a><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif";
mso-fareast-font-family:"Times New Roman"'>- a retired Air Force General, and </span><a
href="http://www.brown.edu/Research/Understanding_the_Iran_Contra_Affair/profile-hakim.php"><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif";
mso-fareast-font-family:"Times New Roman"'>Albert Hakim</span></a><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif";
mso-fareast-font-family:"Times New Roman"'> an Iranian businessmen to supply
the Contras with arms. In November 1984 the three solidified their first agreement
and by the end of the following summer over $11 million in arms were given to
the Contras via private funds. </span><b style='mso-bidi-font-weight:normal'><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'><o:p></o:p></span></b></p>

<p class=MsoNormal style='margin-top:0in;margin-right:18.7pt;margin-bottom:
0in;margin-left:0in;margin-bottom:.0001pt;text-indent:.5in;line-height:200%'><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif";
mso-fareast-font-family:"Times New Roman";mso-bidi-font-style:italic'>Air
Supply Ops</span><span style='font-size:12.0pt;line-height:200%;font-family:
"Times New Roman","serif";mso-fareast-font-family:"Times New Roman"'>: In 1985,
North worked with Secord to build and oversee an air resupply operation for
the contras. A privately funded airstrip was built in Costa Rica in order to
carry out this operation which was functional and successfully delivering arms
to the Contras by May 1986. October 5, 1986 marked the end of the air supply
operations when an aircraft was shot down by the Sandinistas, and crewmember </span><a
href="http://www.brown.edu/Research/Understanding_the_Iran_Contra_Affair/documents/d-all-37.pdf"><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif";
mso-fareast-font-family:"Times New Roman"'>Eugene Hasenfus</span></a><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif";
mso-fareast-font-family:"Times New Roman"'> was captured.<span
style='mso-spacerun:yes'> </span>This would eventually lead to the full
exposure of the operation. <o:p></o:p></span></p>

<p class=MsoNormal style='margin-top:0in;margin-right:18.7pt;margin-bottom:
0in;margin-left:0in;margin-bottom:.0001pt;text-indent:.5in;line-height:200%'><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif";
mso-fareast-font-family:"Times New Roman";mso-bidi-font-style:italic'>Intelligence
Support: </span><span style='font-size:12.0pt;line-height:200%;font-family:
"Times New Roman","serif";mso-fareast-font-family:"Times New Roman"'>North also
provided </span><a
href="http://www.brown.edu/Research/Understanding_the_Iran_Contra_Affair/n-contrasus.php"><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif";
mso-fareast-font-family:"Times New Roman"'>broader strategic military advice</span></a><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif";
mso-fareast-font-family:"Times New Roman"'>.&nbsp; He shared U.S. and CIA
intelligence with the Contras about the location of new Soviet arms and
equipment shipments into Nicaragua. <o:p></o:p></span></p>

<p class=MsoNormal style='margin-top:0in;margin-right:18.7pt;margin-bottom:
0in;margin-left:0in;margin-bottom:.0001pt;line-height:200%'><i
style='mso-bidi-font-style:normal'><u><span style='font-size:12.0pt;line-height:
200%;font-family:"Times New Roman","serif";mso-fareast-font-family:"Times New Roman"'>The
Diversion Scheme<o:p></o:p></span></u></i></p>

<p class=MsoNormal style='margin-top:0in;margin-right:18.7pt;margin-bottom:
0in;margin-left:0in;margin-bottom:.0001pt;text-indent:.5in;line-height:200%'><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif";
mso-fareast-font-family:"Times New Roman"'>When private funding and third party
governments did not provide as much support as North wanted for the Contras, </span><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'>North
came upon the idea of overcharging the Iranians for weapons sold to them by
Americans </span><a
href="http://www.brown.edu/Research/Understanding_the_Iran_Contra_Affair/n-contrasus.php"><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'>and
using the surplus to fund the Contra resupply operation and other covert
activities</span></a><span style='font-size:12.0pt;line-height:200%;font-family:
"Times New Roman","serif"'>. North wrote what would later be infamously known
as the </span><a
href="http://www.gwu.edu/~nsarchiv/NSAEBB/NSAEBB210/16-Diversion%20Memo%204-4-86%20(IC%2002614).pdf"><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'>Diversion
Memo</span></a><span style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'>
to the new National Security Advisor </span><a
href="http://www.brown.edu/Research/Understanding_the_Iran_Contra_Affair/profile-poindexter.php"><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'>John
Poindexter</span></a><span style='font-size:12.0pt;line-height:200%;font-family:
"Times New Roman","serif"'> and President Reagan in which he outlined how $12
MM of the profit Secord and Hakim made from the sale of arms to Iran </span><a
href="http://www.brown.edu/Research/Understanding_the_Iran_Contra_Affair/n-contrasus.php"><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'>will
be used to purchase critically needed supplies for the Nicaraguan Democratic
Resistance Forces</span></a><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif"'>. Of all of the events of the
Iran-Contra Affairs, it was this diversion scheme that was the most
controversial and explosive.<o:p></o:p></span></p>

<p style='margin:0in;margin-bottom:.0001pt;text-indent:.5in;line-height:200%'>Secord
and Hakim were motivated by the potential to profit from the activities they
engaged in, thus the Contras did not receive all the money given to their
cause. As Kagan writes, For all the controversy raised about the diversion,
the Contras were fortunate if they received $2 million worth of tangible
benefits [between January and October 1986,] an amount that paled in comparison
to the far less controversial $32 million they ultimately received from Saudi
Arabia.&nbsp;With government funding, 100% of the money goes directly to the
beneficiary. With third party and private actors, a portion of that will be allocated
as profit.<span style='mso-spacerun:yes'> </span><b style='mso-bidi-font-weight:
normal'><o:p></o:p></b></p>

<p class=MsoNormal style='margin-top:0in;margin-right:18.7pt;margin-bottom:
0in;margin-left:0in;margin-bottom:.0001pt;line-height:200%'><i
style='mso-bidi-font-style:normal'><u><span style='font-size:12.0pt;line-height:
200%;font-family:"Times New Roman","serif"'>Intelligence Authorization Act:
Congress Open to Re-supporting Contras</span></u></i><i style='mso-bidi-font-style:
normal'><u><span style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif";
mso-fareast-font-family:"Times New Roman"'><o:p></o:p></span></u></i></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;text-indent:
.5in;line-height:200%'><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif"'>Congress changed its position on Contra
funding with a series of amendments and provisions that resulted in the
loosening of the Boland language. The Boland Amendments originally aimed to
prevent all funds from flowing into Nicaragua, but from 1985-1986 Congress
began qualifying which funds and for what purposes was acceptable. In August
1985, Congress passed a provision which allocated $14 million directly to the
Contras for humanitarian assistance. Later that year, in December 1985,</span><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif";
mso-fareast-font-family:"Times New Roman"'> as part of the </span><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'>Intelligence
Authorization Act, Congress outlawed most U.S. government departments and
agencies, except for the State Department, from soliciting money from
third-party countries to fund the Contras for humanitarian assistance only.
The State Department was allowed to solicit funds provided that the money
donated was from the countries' own funds and that the U.S. did not enter into
any express or implied arrangement making U.S. provision of assistance to the
third country contingent on the third country's assistance to the
contras.&nbsp;The amendment also included a no quid pro quo statement
between the U.S. and the third country. In 1986, The Intelligence Authorization
Bill allowed the CIA to provide training and intelligence to the Contras as
long as it did not </span><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif";mso-fareast-font-family:"Times New Roman"'>amount
to participation in the planning for execution of military or paramilitary
operations or participation in logistics activities integral to such
operations. In the summer of 1986, Congress passed a provision allocating $100MM
of their own budget in aid to the Contras.</span><span style='font-size:12.0pt;
line-height:200%;font-family:"Times New Roman","serif"'><o:p></o:p></span></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;line-height:
200%'><b style='mso-bidi-font-weight:normal'><u><span style='font-size:12.0pt;
line-height:200%;font-family:"Times New Roman","serif"'>The Iran Story<o:p></o:p></span></u></b></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;line-height:
200%'><i style='mso-bidi-font-style:normal'><u><span style='font-size:12.0pt;
line-height:200%;font-family:"Times New Roman","serif"'>From Secular to Islamic
Republic<o:p></o:p></span></u></i></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;line-height:
200%'><span style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'><span
style='mso-tab-count:1'> </span>As an oil rich nation, Iran is a
country that the U.S. has long held foreign policy interest in. The U.S.
maintained favorable relations with Iran throughout the Shah, </span><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif";
mso-fareast-font-family:"Times New Roman"'>Mohammad Reza Shah Pahlavi's,
secular, yet authoritarian, rule. During those years, Iran was one of the
United States' strongest allies in the Middle East. It was this close
relationship with the U.S. and its foundation in the Shah's secularism that ultimately
served as an impetus for riots and demonstrations to break out across Iran in
1978. These demonstrations grew in strength and number culminating in the Shah
leaving Iran in January 1979 and Ayatollah Ruhollah Khomeini naming Iran an
Islamic Republic. Ayatollah Khomeini immediately severed all ties with the U.S.
and declared Israel an illegitimate country. He ruled Iran as a religious
leader, further consolidating his power. Iran shifted from the U.S.'s most
powerful and valued ally in the Middle East to an American enemy virtually
overnight. <o:p></o:p></span></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;text-indent:
.5in;line-height:200%'><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif";mso-fareast-font-family:"Times New Roman"'>The
U.S., wary of losing its oil rich friend, and desperate to keep Soviets from
influencing the region, quickly moved to normalize relations with Iran. Despite
these efforts, the Muslim Followers of the line of the Imam, a fundamentalist,
anti-imperialist group made up predominately of young radical revolutionaries,
seized the U.S. embassy in Tehran on November 4, 1979, symbolizing the end of
cordial diplomacy between the two nations. Fifty-three hostages were taken by
this group and the Iranian government and general public supported their
actions further antagonizing relations between the two former allies. Although
these hostages were eventually released the day of President Reagan's
inauguration, more hostages would soon be taken, and relations would further be
galvanized.<o:p></o:p></span></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;line-height:
200%'><i style='mso-bidi-font-style:normal'><u><span style='font-size:12.0pt;
line-height:200%;font-family:"Times New Roman","serif";mso-fareast-font-family:
"Times New Roman"'>Iran's need for Weapons Result in U.S. Opportunities<o:p></o:p></span></u></i></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;text-indent:
.5in;line-height:200%'><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif";mso-fareast-font-family:"Times New Roman"'>Iran's
need for weapons during the Iran-Iraq war from 1980-1990 complicated the
Iranian-American relations. In the beginning of the Iran-Iraq war, the U.S.
actively engaged in an arms embargo against Iran called Operation Staunch and religious
fundamentalist group Islamic Holy War took more U.S. hostages beginning in
March 1984. <span style='mso-spacerun:yes'></span>It was Iran's need for
weapons, and the United States' desire to re-open diplomatic relations that in 1985
led Manucher Ghorbanifar, an Iranian businessman working with the U.S., and Adnan
Khashoggi, a Saudi Arabian arms dealer, to devise a skeleton plan for what
would later become the Iran arms deal. This deal would alter Iranian-American relations
and lead to the most controversial piece of the Iran-Contra scandal: the
diversion of funds from the sale of weapons to Iran to supporting the Nicaraguan
Contras. <o:p></o:p></span></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;text-indent:
.5in;line-height:200%'><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif"'>On July 1, 1985, the <em>New York Times</em>
quoted President Ronald Reagan saying, The United States gives terrorists no
rewards. We make no concessions, we make no deals.</span><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif";
mso-fareast-font-family:"Times New Roman"'> Three days later, McFarlane met
with Israeli David Kimche (who had previously met with Khashoggi and
Ghorbanifar) and the arms-for-hostages deal was first outlined as both a means
to obtain the release of American hostages in addition to an attempt to improve
diplomatic relations. Thirteen days after Reagan denounced bartering with
terrorists on July 16, 1985, McFarlane visited President Reagan and his </span><a
href="http://www.brown.edu/Research/Understanding_the_Iran_Contra_Affair/profile-regan.php"><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif";
mso-fareast-font-family:"Times New Roman"'>Chief of Staff Donald Regan</span></a><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif";
mso-fareast-font-family:"Times New Roman"'> while the President was in the hospital
recovering from abdominal surgery. McFarlane proposed their recently outlined arms-for-hostages
deal that specifically called for the selling of 100 American made TOW antitank
missiles to Iran via Israel in exchange for some if not all American hostages
and open communications with Iran. America would also send replacement TOWs to
Israel. There are conflicting accounts of what was said and agreed to at this
meeting. Regan remembered McFarlane saying to the President, they had been
approached by the Israelis, who had had contact that they would put us in touch
with that could lead to a breakthrough in reaching elements in the Government
of Iran and that this could lead to some help in the hostage situation
because we suspected that the Iranians were in some way connected in to the
group who had abducted the Americans.<a style='mso-footnote-id:ftn12'
href="#_ftn12" name="_ftnref12" title=""><span class=MsoFootnoteReference><span
