 style='text-decoration:none'>&nbsp;</span></o:p></span></u></b></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;line-height:
200%'><b style='mso-bidi-font-weight:normal'><u><span style='font-size:12.0pt;
line-height:200%;font-family:"Times New Roman","serif"'>Institutional History:
NSC and CIA<o:p></o:p></span></u></b></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;text-indent:
.5in;line-height:200%'><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif"'>The National Security Council (NSC)
and the Central Intelligence Agency (CIA) developed in such a way that
structurally allowed them to work around Congress and have the Executive Branch
and third party actors implement and frame the foreign policy of the entire
Unites States. To understand how, one must look historically at the evolution
of these two groups. The beginning starts with the National Security Act of
July 26, 1947. Truman signed this piece of legislation that gave birth
simultaneously to both the National Security Council and the Central
Intelligence Agency. <o:p></o:p></span></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;text-indent:
.5in;line-height:200%'><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif"'>The NSC was not originally founded to
facilitate presidential decision making, but it evolved with each
administration until it became structured and powerful enough to perform covert
operations. During Eisenhower's administration in the mid 1950's the NSC became
a virtual adjunct of the presidency.<a style='mso-footnote-id:ftn1'
href="#_ftn1" name="_ftnref1" title=""><span class=MsoFootnoteReference><span
style='mso-special-character:footnote'><![if !supportFootnotes]><span
class=MsoFootnoteReference><span style='font-size:12.0pt;line-height:115%;
font-family:"Times New Roman","serif";mso-fareast-font-family:Calibri;
mso-fareast-theme-font:minor-latin;mso-ansi-language:EN-US;mso-fareast-language:
EN-US;mso-bidi-language:AR-SA'>[1]</span></span><![endif]></span></span></a>
The NSC staff was now under a special assistant to the President and not the
NSC directly, turning the Presidency into a bureaucracy itself. The Kennedy
administration's changes to the NSC were driven by the Bay of Pigs incident
that left Kennedy skeptical of the traditional departments and led him to
prefer a more direct and personal style of executing policies.<span
style='mso-spacerun:yes'> </span>It was under Kennedy that the distinction
between planning and operation was altered.<a style='mso-footnote-id:ftn2'
href="#_ftn2" name="_ftnref2" title=""><span class=MsoFootnoteReference><span
style='mso-special-character:footnote'><![if !supportFootnotes]><span
class=MsoFootnoteReference><span style='font-size:12.0pt;line-height:115%;
font-family:"Times New Roman","serif";mso-fareast-font-family:Calibri;
mso-fareast-theme-font:minor-latin;mso-ansi-language:EN-US;mso-fareast-language:
EN-US;mso-bidi-language:AR-SA'>[2]</span></span><![endif]></span></span></a>
Whereas the NSC was previously a planning entity, Kennedy made it also function
operationally. This allowed the executive branch to avoid going through the
State Department. This marked a trend of inflating the Office of the President
and its replication of the rest of the government. The Office of the President
grew in ways that sometimes supported, sometimes competed with, and other times
ignored other governmental agencies and offices. This trend continued with the
Reagan administration. The NSC became further professionalized with a staff of
about forty-five under the National Security Advisor </span><a
href="http://www.brown.edu/Research/Understanding_the_Iran_Contra_Affair/profile-mcfarlane.php"><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'>Robert
McFarlane</span></a><span style='font-size:12.0pt;line-height:200%;font-family:
"Times New Roman","serif"'> and more than 200 people in support.<a
style='mso-footnote-id:ftn3' href="#_ftn3" name="_ftnref3" title=""><span
class=MsoFootnoteReference><span style='mso-special-character:footnote'><![if !supportFootnotes]><span
class=MsoFootnoteReference><span style='font-size:12.0pt;line-height:115%;
font-family:"Times New Roman","serif";mso-fareast-font-family:Calibri;
mso-fareast-theme-font:minor-latin;mso-ansi-language:EN-US;mso-fareast-language:
EN-US;mso-bidi-language:AR-SA'>[3]</span></span><![endif]></span></span></a> It
became further structured in reflection of the State Department under Robert
McFarlane's successor, </span><a
href="http://www.brown.edu/Research/Understanding_the_Iran_Contra_Affair/profile-poindexter.php"><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'>John
Poindexter</span></a><span style='font-size:12.0pt;line-height:200%;font-family:
"Times New Roman","serif"'> when it was organized into twelve directorates i.e.
the African office, European Office, etc. The person most hurt, and most
undermined by this trend was the Secretary of State, </span><a
href="http://www.brown.edu/Research/Understanding_the_Iran_Contra_Affair/profile-shultz.php"><span
style='font-size:12.0pt;line-height:200%;font-family:"Times New Roman","serif"'>George
Shultz</span></a><span style='font-size:12.0pt;line-height:200%;font-family:
"Times New Roman","serif"'> during the Reagan administration, because now the
president was performing similar duties, with similar staff support from his
own office. The NSC was now large and varied enough to carry out the
president's wishes covertly- even from the rest of the government.<a
style='mso-footnote-id:ftn4' href="#_ftn4" name="_ftnref4" title=""><span
class=MsoFootnoteReference><span style='mso-special-character:footnote'><![if !supportFootnotes]><span
