line-height:200%;font-family:"Times New Roman","serif";mso-fareast-font-family:
"Times New Roman"'>The Enterprise<o:p></o:p></span></u></i></p>

<p style='margin:0in;margin-bottom:.0001pt;text-indent:.5in;line-height:200%'><span
style='mso-spacerun:yes'></span>On <span style='mso-bidi-font-weight:bold'>August
20, 1985,</span> the first load of 96 TOW missiles was sent from Israel to
Iran. Khashoggi provided bridge financing, posting $1 million of his private
funds until Iran paid Israel for the weapons.<a style='mso-footnote-id:ftn16'
href="#_ftn16" name="_ftnref16" title=""><span class=MsoFootnoteReference><span
style='mso-special-character:footnote'><![if !supportFootnotes]><span
class=MsoFootnoteReference><span style='font-size:12.0pt;line-height:115%;
