style='font-size:12.0pt;line-height:115%;font-family:"Times New Roman","serif";
mso-fareast-font-family:"Times New Roman";mso-ansi-language:EN-US;mso-fareast-language:
EN-US;mso-bidi-language:AR-SA'>[23]</span></span><![endif]></span></span></a> This
shipment method did not run smoothly either, running across problems in Cyprus,
Turkey, and culminating in the realization that the wrong missiles had been
sent once they reached Tehran. The horror story ended with the decision to immediately
repeat the operation under U.S. instead of Israeli management, this time with
more success. </p>

<p style='margin:0in;margin-bottom:.0001pt;text-indent:.5in;line-height:200%'>After
experiencing difficulties such as the second arms shipment and problems in
securing the discussed exchanges with Iran, on January 17, 1986, President
Reagan signed a <a
href="http://www.gwu.edu/~nsarchiv/NSAEBB/NSAEBB210/15-Reagan%20Finding%201-17-86%20(IC%2002181).pdf">Presidential
Finding</a> authorizing direct U.S. arms sales to Iran. Secord and the
Enterprise would still be used as a third party to release the U.S. of any
liability. Israel, would still serve as the base, but it would no longer buy
and sell weapons. Now the Enterprise would buy and sell weapons directly on
behalf of the United States. </p>

<p style='margin:0in;margin-bottom:.0001pt;line-height:200%'><i
style='mso-bidi-font-style:normal'><u>The Second Channel<o:p></o:p></u></i></p>

<p style='margin:0in;margin-bottom:.0001pt;text-indent:.5in;line-height:200%'>After
continuing difficulties in securing the release of hostages from Iran, North
and Secord determined that the U.S. had to find an alternate channel for
dealing with Iran, and put Hakim in charge of the effort. In August 1986,
Hakim, with his new Iranian contact, Ali Hashemi Bahramani, worked out a <a
href="http://www.brown.edu/Research/Understanding_the_Iran_Contra_Affair/documents/d-all-38.pdf">nine-point
plan</a> that compromised both his and the Iranians' interests. The resulting
agreement was that the U.S. would send Iran 1,500 TOWs in exchange for the
release of 1.5 hostages (1 definitely and the 2<sup>nd</sup> with all
effective possible effort). Iran also offered to pay the U.S. $3.6 million in
addition to releasing the hostages which meant more funds could be diverted to
the Contras. Hakim, serving as a U.S. representative, implemented his
nine-point plan beginning in October 28, 1986 with the first shipment of arms. Of
the $3.6 million Iran paid to the Enterprise, $2 million of this was given to
the CIA who supplied the weapons, and the remaining $1.6 million was diverted
to the Contras. </p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;line-height:
200%'><b style='mso-bidi-font-weight:normal'><u><span style='font-size:12.0pt;
line-height:200%;font-family:"Times New Roman","serif"'>Unraveling the Story<o:p></o:p></span></u></b></p>

<p style='margin:0in;margin-bottom:.0001pt;text-indent:.5in;line-height:200%'>On
<span style='mso-bidi-font-weight:bold'>November 3, 1986,</span> two Lebanese
newspapers broke the story of the Iran arms deal, and quickly thereafter the
entire scandal began to unravel in the United States. The first two weeks following
the newspaper leak were marked by an increasing crisis of confidence in the
government as facts rapidly became public. The month of November was marked by
heavy media coverage, and by December 1986 everything from the Contra affair to
the diversion scheme found its way into the press. </p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;text-indent:
.5in;line-height:200%'><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif";mso-fareast-font-family:"Times New Roman";
mso-bidi-font-weight:bold'>Nov. 13, 1986</span><span style='font-size:12.0pt;
line-height:200%;font-family:"Times New Roman","serif";mso-fareast-font-family:
"Times New Roman"'>: President Ronald Reagan made his <u><span
style='color:blue'>Address to the Nation on the Iran Arms and Contra Aid
Controversy </span></u></span><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif"'>and again addressed the nation in a
press conference on November 19<sup>th</sup>. On the 13<sup>th</sup>, Reagan
said that the U.S. was working with the Iranian government, but on the 19<sup>th</sup>,
he admitted to working with a particular group,<a style='mso-footnote-id:
ftn24' href="#_ftn24" name="_ftnref24" title=""><span
class=MsoFootnoteReference><span style='mso-special-character:footnote'><![if !supportFootnotes]><span
