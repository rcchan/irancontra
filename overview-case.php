mso-ansi-language:EN-US;mso-fareast-language:EN-US;mso-bidi-language:AR-SA'>[20]</span></span><![endif]></span></span></a>
Schwimmer, who was heading the Israeli operations, applied last minute to get
the necessary special clearances to land the cargo of arms at the Portuguese
airport in Lisbon. Schwimmer found the authorities hesitant to grant him
permission. It was at this point that Oliver North got involved and met with
Israeli Defense Minister Rabin in New York on November 18<sup>th</sup> about
the operational logistics of shipping the oil drilling equipment to Iran.<a
style='mso-footnote-id:ftn21' href="#_ftn21" name="_ftnref21" title=""><span
class=MsoFootnoteReference><span style='mso-special-character:footnote'><![if !supportFootnotes]><span
