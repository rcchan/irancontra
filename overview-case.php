EN-US;mso-bidi-language:AR-SA'>[22]</span></span><![endif]></span></span></a> </p>

<p style='margin:0in;margin-bottom:.0001pt;text-indent:.5in;line-height:200%'>Ultimately
North and Clarridge used an alternate plan utilizing proprietary airline
flights to make the arms shipment. A proprietary airline is such that it is
owned and controlled by the CIA but operates as if it were an ordinary commercial
operation when not utilized for special CIA assignment. When Clarridge decided
to go the proprietary route he informed the CIA controller in Frankfurt that
an urgent fight that was in the interest of the U.S. government would need
to be flown.<a style='mso-footnote-id:ftn23' href="#_ftn23" name="_ftnref23"
title=""><span class=MsoFootnoteReference><span style='mso-special-character:
