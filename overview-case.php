 style='text-decoration:none'>&nbsp;</span></o:p></span></u></b></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;text-indent:
.5in;line-height:200%'><span style='font-size:12.0pt;line-height:200%;
font-family:"Times New Roman","serif"'>The
Iran-Contra Affair of 1984-1987 was not one affair but two separate covert foreign
policy issues concerning two different problems, in two separate countries,
that were dealt in two very different ways. Under the management of the same
few officials, both the Iran and the Contra policies intersected at certain
points giving rise to the singular title, Iran-Contra Affair.<span
style='mso-spacerun:yes'> </span>The first covert foreign policy initiative
was the continued support for the democratic rebel Contras against the
communist Sandinistas in Nicaragua in a time when Congress had cut off funds to
the Contras. The second covert foreign policy initiative was the selling of
arms to Iran in exchange for the release of American hostages held by Iranian
allies in Lebanon. The two policies intersected when profits from the arms
sales to Iran were used to support the Nicaraguan Contras through third parties
and private funds. </p>

