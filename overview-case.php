<p style='margin:0in;margin-bottom:.0001pt'><i style='mso-bidi-font-style:normal'>The
common ingredients of the Iran and Contra policies were secrecy, deception, and
disdain for the law...the United States simultaneously pursued two
contradictory foreign policies a public one and a secret one (</i><a
href="http://www.archive.org/details/reportofcongress87unit"><i
style='mso-bidi-font-style:normal'>Report of the Congressional Committees
