<h3>OLIVER NORTH (22) - <a href="profile-north.php" class="content_link">Profile</a></h3>
<p>On July 7, 1987, a headline on the front of <em>The Wall Street Journal</em> asked a critical question: “Which Col. North Will Tell His Story to Nation: The Villain Who Deceived or Hero Who Obeyed?”&nbsp; Readers of <em>Washingtonian</em> magazine had voted Lt. Col. Oliver North the top villain of the year—and the biggest hero.&nbsp; It would have been an understatement to call “Ollie” a conundrum.&nbsp; Without a doubt, North was expected to be the star witness of the hearings.&nbsp; Starting the morning of the 7th, he did not disappoint.&nbsp; Just as the American people were listening to North’s testimony from day one, they were also watching the spectacle.&nbsp; “The day-long show proved again that Washington can still outdo Hollywood in the production of high-yield dramatic blockbusters,” declared <em>The Washington Post</em>.&nbsp; “Was he the good guy, the bad guy or the fall guy?” asked The<em> Chicago Tribune</em>.&nbsp; “On Tuesday he revealed himself as the wise guy.”</p>
<p>Taking place over six straight weekdays, North provided plenty of substance, too.&nbsp; At the start, he vowed, “I came here to tell you the truth—the good, the bad, and the ugly.”&nbsp; He acknowledged shredding key documents, but North maintained that his actions were in line with what his superiors wanted.&nbsp; North claimed that he saw a signed presidential Finding to ship arms to Iran and that President Reagan was aware of the private fundraising for the Contras.&nbsp; He also disclosed that the late CIA Director William Casey was aware of North’s activities, but he deliberately kept the secretaries of state and defense out of the loop.&nbsp; Indeed, North aimed to ensure “plausible deniability” among his superiors (including Reagan).<br />
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <br />
  For all their serious topics, the North hearings will rightly be remembered in part due to their entertainment value.&nbsp; When questioned about using Enterprise money for a home security system, North told a compelling story about being willing to subject himself, but not his family, to a fight with terrorists.&nbsp; Referring to a prominent terrorist, he said, “I will be glad to meet Abu Nidal on equal terms anywhere in the world.”&nbsp; Dramatically, he defended his actions by saying that “lying does not come easy” to him, but he had to consider “the difference between lives and lies.”&nbsp; Brendan Sullivan, North’s tenacious lawyer, also joined in on the theatrics.&nbsp; By far the most vocal of all the witnesses’ lawyers, Sullivan objected frequently and declared that he was there as a lawyer, “not a potted plant.”&nbsp; The panel members became emotive, as well.&nbsp; Sen. Orrin Hatch (R-UT) made a public plea against future prosecution for North; Co-Chairman Rep. Lee Hamilton (D-IN) lectured North on patriotism.</p>
<p>Finally, Co-Chairman Sen. Daniel Inouye (D-HI), a World War II hero, wrapped up the six days with a memorable speech.&nbsp; Inouye reminded North about cadet honor, declaring, “Members of the military have an obligation to disobey unlawful orders.”&nbsp; Inouye made reference to the Nuremberg Trials, which set off Sullivan, who interrupted the co-chairman to say that his closing statement was “personally and professionally distasteful.”&nbsp; Undeterred, Inouye responded and then finished his speech with a Thomas Jefferson quotation about the importance of “the spirit of resistance to government.”</p>
<p>With that, on July 14, North’s week of testimony came to an end.&nbsp; As <em>The Washington Post </em>wrote, this section of the hearings proved to be “suspenseful, eventful and surprising up to the last scene of the last act.”</p>
